import { logout } from "@/app/actions/auth";
import { getToken } from "@/lib/token";
import { ScopeEnum } from "@/lib/types/strapi-autogenerated";
import { redirect } from "next/navigation";
import api from "../api";
import AddFollowerForm from "./add-follower-form";
import FollowButton from "./follow-button";
import UnfollowButton from "./unfollow-button";

export default async function DashboardPage() {
  const token = await getToken();

  if (!token) {
    redirect("/login");
  }

  const user =
    await api.usersPermissionsUsersRoles.getUsersPermissionsUsersRoles();

  const followers = await api.follower.getFollowers({
    scope: ScopeEnum.Following,
    populate: "*",
  });

  const notFollowing = await api.follower.getFollowers({
    scope: ScopeEnum.Discover,
    populate: "*",
  });

  const recordings = await api.recording.getRecordings({
    scope: ScopeEnum.Following,
    populate: "*",
  });

  const recordingsNotFollowing = await api.recording.getRecordings({
    scope: ScopeEnum.Discover,
    populate: "*",
  });

  return (
    <div style={{ maxWidth: 800, margin: "40px auto", padding: 20 }}>
      <div
        style={{
          display: "flex",
          justifyContent: "space-between",
          marginBottom: 32,
        }}
      >
        <h1>Dashboard</h1>
        <div>
          <span style={{ marginRight: 16 }}>Hi, {user?.data?.username}</span>
          <form action={logout} style={{ display: "inline" }}>
            <button type="submit">Logout</button>
          </form>
        </div>
      </div>

      <AddFollowerForm />

      {/* Followers Section */}
      <div
        style={{
          display: "grid",
          gridTemplateColumns: "1fr 1fr",
          gap: 32,
          marginBottom: 32,
        }}
      >
        {/* Following */}
        <div>
          <h2 style={{ marginBottom: 16 }}>
            Following ({followers.data?.data?.length || 0})
          </h2>
          {followers.data?.data?.length === 0 ? (
            <p style={{ color: "#666" }}>Youre not following anyone yet.</p>
          ) : (
            <ul style={{ listStyle: "none", padding: 0 }}>
              {followers.data?.data?.map((f) => (
                <li
                  key={f.id}
                  style={{
                    display: "flex",
                    justifyContent: "space-between",
                    alignItems: "center",
                    padding: 12,
                    border: "1px solid #eee",
                    marginBottom: 8,
                    borderRadius: 8,
                  }}
                >
                  <div>
                    <strong>{f.username}</strong>{" "}
                    <span style={{ color: "#666", fontSize: 12 }}>
                      @{f.slug}
                    </span>
                    <br />
                    <small style={{ color: "#999" }}>
                      {f.totalRecordings} recordings
                    </small>
                  </div>
                  <UnfollowButton id={f.id!} />
                </li>
              ))}
            </ul>
          )}
        </div>

        {/* Not Following */}
        <div>
          <h2 style={{ marginBottom: 16 }}>
            Discover ({notFollowing.data?.data?.length || 0})
          </h2>
          {notFollowing.data?.data?.length === 0 ? (
            <p style={{ color: "#666" }}>No more accounts to discover.</p>
          ) : (
            <ul style={{ listStyle: "none", padding: 0 }}>
              {notFollowing.data?.data?.map((f) => (
                <li
                  key={f.id}
                  style={{
                    display: "flex",
                    justifyContent: "space-between",
                    alignItems: "center",
                    padding: 12,
                    border: "1px solid #eee",
                    marginBottom: 8,
                    borderRadius: 8,
                  }}
                >
                  <div>
                    <strong>{f.username}</strong>{" "}
                    <span style={{ color: "#666", fontSize: 12 }}>
                      @{f.slug}
                    </span>
                    <br />
                    <small style={{ color: "#999" }}>
                      {f.totalRecordings} recordings
                    </small>
                  </div>
                  <FollowButton
                    username={f.username}
                    slug={f.slug}
                    type={f.type}
                  />
                </li>
              ))}
            </ul>
          )}
        </div>
      </div>

      {/* Recordings Section */}
      <div style={{ display: "grid", gridTemplateColumns: "1fr 1fr", gap: 32 }}>
        {/* Recordings from followed */}
        <div>
          <h2 style={{ marginBottom: 16 }}>
            My Feed ({recordings.data?.data?.length || 0})
          </h2>
          {recordings.data?.data?.length === 0 ? (
            <p style={{ color: "#666" }}>
              No recordings from followed accounts.
            </p>
          ) : (
            <ul style={{ listStyle: "none", padding: 0 }}>
              {recordings.data?.data?.map((r) => (
                <li
                  key={r.id}
                  style={{
                    padding: 12,
                    border: "1px solid #eee",
                    marginBottom: 8,
                    borderRadius: 8,
                  }}
                >
                  <div style={{ marginBottom: 8 }}>
                    <strong>{r.follower?.username}</strong>{" "}
                    <span style={{ color: "#666", fontSize: 12 }}>
                      @{r.follower?.slug}
                    </span>
                  </div>
                  <div style={{ fontSize: 12, color: "#999", marginBottom: 8 }}>
                    {r.createdAt
                      ? new Date(r.createdAt).toLocaleString()
                      : "Unknown date"}
                  </div>
                  {r.sources && r.sources.length > 0 && (
                    <div>
                      <small style={{ color: "#666" }}>
                        {r.sources.length} source(s)
                      </small>
                      <ul style={{ margin: "8px 0", paddingLeft: 20 }}>
                        {r.sources.map((s) => (
                          <li key={s.id} style={{ fontSize: 12 }}>
                            <a
                              href={s.path || "#"}
                              target="_blank"
                              rel="noopener noreferrer"
                              style={{ color: "#0066cc" }}
                            >
                              {s.path?.split("/").pop() || "View"}
                            </a>
                            {s.duration && (
                              <span style={{ color: "#999", marginLeft: 8 }}>
                                {Math.floor(s.duration / 60)}m {s.duration % 60}
                                s
                              </span>
                            )}
                          </li>
                        ))}
                      </ul>
                    </div>
                  )}
                </li>
              ))}
            </ul>
          )}
        </div>

        {/* Recordings from not followed (discover) */}
        <div>
          <h2 style={{ marginBottom: 16 }}>
            Discover Recordings (
            {recordingsNotFollowing.data?.data?.length || 0})
          </h2>
          {recordingsNotFollowing.data?.data?.length === 0 ? (
            <p style={{ color: "#666" }}>No recordings to discover.</p>
          ) : (
            <ul style={{ listStyle: "none", padding: 0 }}>
              {recordingsNotFollowing.data?.data?.map((r) => (
                <li
                  key={r.id}
                  style={{
                    padding: 12,
                    border: "1px solid #eee",
                    marginBottom: 8,
                    borderRadius: 8,
                  }}
                >
                  <div
                    style={{
                      display: "flex",
                      justifyContent: "space-between",
                      marginBottom: 8,
                    }}
                  >
                    <div>
                      <strong>{r.follower?.username}</strong>{" "}
                      <span style={{ color: "#666", fontSize: 12 }}>
                        @{r.follower?.slug}
                      </span>
                    </div>
                    {r.follower && (
                      <FollowButton
                        username={r.follower.username || ""}
                        slug={r.follower.slug || ""}
                        type={r.follower.type || "tiktok"}
                      />
                    )}
                  </div>
                  <div style={{ fontSize: 12, color: "#999", marginBottom: 8 }}>
                    {r.createdAt
                      ? new Date(r.createdAt).toLocaleString()
                      : "Unknown date"}
                  </div>
                  {r.sources && r.sources.length > 0 && (
                    <div>
                      <small style={{ color: "#666" }}>
                        {r.sources.length} source(s)
                      </small>
                      <ul style={{ margin: "8px 0", paddingLeft: 20 }}>
                        {r.sources.map((s) => (
                          <li key={s.id} style={{ fontSize: 12 }}>
                            <a
                              href={s.path || "#"}
                              target="_blank"
                              rel="noopener noreferrer"
                              style={{ color: "#0066cc" }}
                            >
                              {s.path?.split("/").pop() || "View"}
                            </a>
                            {s.duration && (
                              <span style={{ color: "#999", marginLeft: 8 }}>
                                {Math.floor(s.duration / 60)}m {s.duration % 60}
                                s
                              </span>
                            )}
                          </li>
                        ))}
                      </ul>
                    </div>
                  )}
                </li>
              ))}
            </ul>
          )}
        </div>
      </div>
    </div>
  );
}
