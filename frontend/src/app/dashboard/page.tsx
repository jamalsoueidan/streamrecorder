import { logout } from "@/app/actions/auth";
import { getToken } from "@/lib/token";
import { ScopeEnum } from "@/lib/types/strapi-autogenerated";
import { redirect } from "next/navigation";
import api from "../api";
import AddFollowerForm from "./add-follower-form";
import Followings from "./followings";
import Recordings from "./recordings";

export default async function DashboardPage() {
  const token = await getToken();

  if (!token) {
    redirect("/login");
  }

  const user =
    await api.usersPermissionsUsersRoles.getUsersPermissionsUsersRoles();

  const defaultScope = ScopeEnum.Following;

  const [followersResponse, recordingsResponse] = await Promise.all([
    api.follower.getFollowers({ scope: defaultScope, populate: "*" }),
    api.recording.getRecordings({ scope: defaultScope, populate: "*" }),
  ]);

  return (
    <div style={{ maxWidth: 800, margin: "0 auto", padding: 20 }}>
      <header style={{ display: "flex", justifyContent: "space-between", marginBottom: 24 }}>
        <h1>Dashboard</h1>
        <div>
          <span>Hi, {user?.data?.username}</span>
          <form action={logout} style={{ display: "inline", marginLeft: 12 }}>
            <button type="submit">Logout</button>
          </form>
        </div>
      </header>

      <AddFollowerForm />

      <div style={{ display: "grid", gridTemplateColumns: "1fr 1fr", gap: 24 }}>
        <Followings
          initialData={followersResponse.data?.data || []}
          initialScope={defaultScope}
        />

        <Recordings
          initialData={recordingsResponse.data?.data || []}
          initialScope={defaultScope}
        />
      </div>
    </div>
  );
}
