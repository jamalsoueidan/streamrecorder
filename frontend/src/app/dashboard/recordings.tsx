"use client";

import { useState, useTransition } from "react";
import { getRecordingsData } from "@/app/actions/recordings";
import { ScopeEnum, FollowRequestBodyTypeEnum, Recording } from "@/lib/types/strapi-autogenerated";
import FollowButton from "./follow-button";

type Props = {
  initialData: Recording[];
  initialScope: ScopeEnum | "all";
};

export default function Recordings({ initialData, initialScope }: Props) {
  const [data, setData] = useState(initialData);
  const [scope, setScope] = useState(initialScope);
  const [isPending, startTransition] = useTransition();

  const loadData = (newScope?: ScopeEnum | "all") => {
    const targetScope = newScope ?? scope;
    startTransition(async () => {
      const result = await getRecordingsData(targetScope);
      setData(result);
      if (newScope) setScope(newScope);
    });
  };

  return (
    <section>
      <header style={{ display: "flex", justifyContent: "space-between", alignItems: "center", marginBottom: 12 }}>
        <h2>Recordings ({data.length})</h2>
        <div>
          <select
            value={scope}
            onChange={(e) => loadData(e.target.value as ScopeEnum | "all")}
          >
            <option value={ScopeEnum.Following}>Following</option>
            <option value={ScopeEnum.Discover}>Discover</option>
            <option value="all">All</option>
          </select>
          {isPending && <span> Loading...</span>}
        </div>
      </header>

      {data.length === 0 ? (
        <p>
          {scope === ScopeEnum.Following
            ? "No recordings from followed accounts."
            : scope === ScopeEnum.Discover
            ? "No recordings to discover."
            : "No recordings found."}
        </p>
      ) : (
        <ul style={{ listStyle: "none", padding: 0 }}>
          {data.map((r) => (
            <li key={r.id} style={{ marginBottom: 12 }}>
              <div style={{ display: "flex", justifyContent: "space-between" }}>
                <span>
                  <strong>{r.follower?.username}</strong> @{r.follower?.slug}
                </span>
                {r.follower && scope !== ScopeEnum.Following && (
                  <FollowButton
                    username={r.follower.username || ""}
                    slug={r.follower.slug || ""}
                    type={(r.follower.type || "tiktok") as FollowRequestBodyTypeEnum}
                    onSuccess={() => loadData()}
                  />
                )}
              </div>
              <small>{r.createdAt ? new Date(r.createdAt).toLocaleString() : "Unknown date"}</small>
              {r.sources && r.sources.length > 0 && (
                <ul style={{ marginTop: 4 }}>
                  {r.sources.map((s) => (
                    <li key={s.id}>
                      <a href={s.path || "#"} target="_blank" rel="noopener noreferrer">
                        {s.path?.split("/").pop() || "View"}
                      </a>
                      {s.duration && (
                        <span> ({Math.floor(s.duration / 60)}m {s.duration % 60}s)</span>
                      )}
                    </li>
                  ))}
                </ul>
              )}
            </li>
          ))}
        </ul>
      )}
    </section>
  );
}
