import { FollowerTypeEnum } from "@/lib/types/strapi-autogenerated";
import { ThemeIconProps } from "@mantine/core";

type FollowerTypeTextProps = {
  type: FollowerTypeEnum | undefined;
} & ThemeIconProps;

export const FollowerTypeText = ({ type }: FollowerTypeTextProps) => {
  return (
    <>
      {type === FollowerTypeEnum.Tiktok && <>Tiktok</>}
      {type === FollowerTypeEnum.Twitch && <>Twitch</>}
      {type === FollowerTypeEnum.Kick && <>Kick</>}
      {type === FollowerTypeEnum.Afreecatv && <>Afreecatv</>}
      {type === FollowerTypeEnum.Youtube && <>Youtube</>}
    </>
  );
};
