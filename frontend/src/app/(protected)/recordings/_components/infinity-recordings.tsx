// components/infinite-followers.tsx
"use client";

import { getRecordings } from "@/app/actions/recordings";
import {
  RecordingListResponse,
  ScopeEnum,
} from "@/lib/types/strapi-autogenerated";
import {
  Anchor,
  Avatar,
  Divider,
  Flex,
  Grid,
  Group,
  Image,
  Loader,
  Select,
  Stack,
  Text,
  Title,
} from "@mantine/core";
import { useIntersection } from "@mantine/hooks";
import Link from "next/link";
import { parseAsStringEnum, useQueryState } from "nuqs";
import { useEffect, useState, useTransition } from "react";

enum Sort {
  createdAtDesc = "createdAt:desc",
  createdAtAsc = "createdAt:asc",
}

const SORT_OPTIONS = [
  { value: Sort.createdAtDesc, label: "Newest first" },
  { value: Sort.createdAtAsc, label: "Oldest first" },
];

interface Props {
  initialData: RecordingListResponse["data"];
  initialPagination: RecordingListResponse["meta"];
  initialSort?: string;
}

export default function InfiniteRecordings({
  initialData,
  initialPagination,
  initialSort,
}: Props) {
  const [data, setData] = useState(initialData);
  const [page, setPage] = useState(initialPagination?.pagination?.page ?? 1);
  const [hasMore, setHasMore] = useState(
    page < (initialPagination?.pagination?.pageCount ?? 1)
  );
  const [sort, setSort] = useQueryState(
    "sort",
    parseAsStringEnum<Sort>(Object.values(Sort)).withDefault(
      initialSort as unknown as Sort
    )
  );
  const [isPending, startTransition] = useTransition();

  const { ref, entry } = useIntersection({
    threshold: 0.5,
  });

  const handleSortChange = (value: string | null) => {
    if (!value) return;

    setSort(value as unknown as Sort);
    startTransition(async () => {
      const result = await getRecordings(ScopeEnum.Discover, 1, value);
      setData(result.data);
      setPage(1);
      setHasMore(1 < (result.meta?.pagination?.pageCount ?? 1));
    });
  };

  useEffect(() => {
    if (entry?.isIntersecting && hasMore && !isPending) {
      startTransition(async () => {
        const nextPage = page + 1;
        const result = await getRecordings(ScopeEnum.Discover, nextPage, sort);

        setData((prev) => {
          const existingIds = new Set(prev?.map((f) => f.id));
          const newItems = result.data.filter((f) => !existingIds.has(f.id));
          return [...(prev || []), ...newItems];
        });
        setPage(nextPage);
        setHasMore(nextPage < (result.meta?.pagination?.pageCount ?? 1));
      });
    }
  }, [entry?.isIntersecting, hasMore, isPending, page, sort]);

  return (
    <Stack gap="sm">
      <Group justify="space-between">
        <Title order={1} size="lg">
          Recordings ({initialPagination?.pagination?.total || 0})
        </Title>

        <Select
          key={sort}
          size="xs"
          w={180}
          value={sort}
          onChange={handleSortChange}
          data={SORT_OPTIONS}
          disabled={isPending}
        />
      </Group>
      <Divider />
      <Flex gap="lg" wrap="wrap">
        {(data || []).map((f) => (
          <Grid gutter="xs" key={f.documentId} w="160">
            <Grid.Col span={12}>
              <Anchor
                component={Link}
                href={`/${f.follower?.type}/${f.follower?.username}/live/${f.documentId}`}
              >
                <Image
                  key={f.documentId}
                  alt={f.follower?.username || "unknown"}
                  radius="md"
                  src={
                    f.sources?.length
                      ? process.env.NEXT_PUBLIC_S3_URL! +
                        f.sources[0].path +
                        "preview.jpg"
                      : null
                  }
                  w={160}
                  h={284}
                  loading="lazy"
                  fallbackSrc="https://placehold.co/600x400?text=Placeholder"
                  style={{
                    objectFit: "cover",
                    objectPosition: "center center",
                    flexShrink: 0,
                  }}
                />
              </Anchor>
            </Grid.Col>
            <Grid.Col span={12}>
              <Anchor
                component={Link}
                href={`/${f.follower?.type}/${f.follower?.username}`}
              >
                <Group gap="xs">
                  <Avatar
                    size={34}
                    src={
                      f.sources?.at(0)?.path
                        ? process.env.NEXT_PUBLIC_S3_URL! +
                          f.sources?.at(0)?.path +
                          "preview.jpg"
                        : null
                    }
                    styles={{
                      image: {
                        transform: "scale(2)",
                        objectFit: "cover",
                      },
                    }}
                  />

                  <div>
                    <Title order={2} size="md">
                      {f.follower?.username}
                    </Title>
                    <Text size="xs">
                      {new Date(f.createdAt || "").toLocaleString("en-US", {
                        dateStyle: "short",
                        timeStyle: "short",
                      })}
                    </Text>
                  </div>
                </Group>
              </Anchor>
            </Grid.Col>
          </Grid>
        ))}
      </Flex>
      {/* Sentinel element */}
      <div ref={ref} style={{ height: 1 }} />

      {isPending && <Loader size="sm" style={{ alignSelf: "center" }} />}

      {!hasMore && data && data.length > 0 && (
        <p style={{ textAlign: "center", color: "gray" }}>No more to load</p>
      )}
    </Stack>
  );
}
