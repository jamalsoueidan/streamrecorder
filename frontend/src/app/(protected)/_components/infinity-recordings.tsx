// components/infinite-followers.tsx
"use client";

import { getRecordings } from "@/app/actions/recordings";

import dayjs from "@/app/lib/dayjs";
import { SortOptions } from "@/lib/types/filtering";
import {
  RecordingListResponse,
  ScopeEnum,
} from "@/lib/types/strapi-autogenerated";
import {
  Anchor,
  Avatar,
  Divider,
  Grid,
  Group,
  Loader,
  Select,
  SimpleGrid,
  Stack,
  Text,
  Title,
} from "@mantine/core";
import { useIntersection } from "@mantine/hooks";
import Link from "next/link";
import { parseAsStringEnum, useQueryState } from "nuqs";
import { useEffect, useRef, useState, useTransition } from "react";
import { ImageVideoPreview } from "./image-video-preview";

const SORT_OPTIONS = [
  { value: SortOptions.createdAtDesc, label: "Newest first" },
  { value: SortOptions.createdAtAsc, label: "Oldest first" },
];

interface Props {
  initialData: RecordingListResponse["data"];
  initialPagination: RecordingListResponse["meta"];
  initialSort?: SortOptions;
  fetchAction: (
    options: Parameters<typeof getRecordings>[0]
  ) => ReturnType<typeof getRecordings>;
}

export default function InfiniteRecordings({
  initialData,
  initialPagination,
  initialSort,
  fetchAction,
}: Props) {
  const [data, setData] = useState(initialData);
  const [page, setPage] = useState(initialPagination?.pagination?.page ?? 1);
  const [hasMore, setHasMore] = useState(
    page < (initialPagination?.pagination?.pageCount ?? 1)
  );
  const [sort, setSort] = useQueryState(
    "sort",
    parseAsStringEnum<SortOptions>(Object.values(SortOptions)).withDefault(
      initialSort || SortOptions.createdAtDesc
    )
  );

  const [isPending, startTransition] = useTransition();

  const { ref, entry } = useIntersection({
    threshold: 0.5,
  });

  const handleSortChange = (value: string | null) => {
    if (!value) return;
    setSort(value as unknown as SortOptions);
  };

  const isFirstRender = useRef(true);

  // 1. Refetch when sort changes
  useEffect(() => {
    if (isFirstRender.current) {
      isFirstRender.current = false;
      return;
    }

    startTransition(async () => {
      const result = await fetchAction({
        scope: ScopeEnum.Following,
        "pagination[page]": 1,
        sort,
      });
      setData(result.data);
      setPage(1);
      setHasMore(1 < (result.meta?.pagination?.pageCount ?? 1));
    });
  }, [fetchAction, sort]);

  useEffect(() => {
    if (entry?.isIntersecting && hasMore && !isPending) {
      startTransition(async () => {
        const nextPage = page + 1;
        const result = await fetchAction({
          scope: ScopeEnum.Following,
          "pagination[page]": nextPage,
          sort,
        });

        setData((prev) => {
          const existingIds = new Set(prev?.map((f) => f.id));
          const newItems = result.data.filter((f) => !existingIds.has(f.id));
          return [...(prev || []), ...newItems];
        });
        setPage(nextPage);
        setHasMore(nextPage < (result.meta?.pagination?.pageCount ?? 1));
      });
    }
  }, [entry?.isIntersecting, hasMore, isPending, page, sort, fetchAction]);

  return (
    <Stack gap="sm">
      <Group justify="space-between">
        <Title order={1} size="lg">
          Recordings ({initialPagination?.pagination?.total || 0})
        </Title>

        <Select
          key={sort}
          size="xs"
          w={180}
          value={sort}
          onChange={handleSortChange}
          data={SORT_OPTIONS}
          disabled={isPending}
        />
      </Group>
      <Divider />
      <SimpleGrid cols={{ base: 1, sm: 2, md: 3, xl: 4 }} spacing="lg">
        {(data || []).map((rec) => {
          if (rec.follower?.username === "stefany_23v") {
            console.log(rec);
          }
          return (
            <Grid gutter="xs" key={rec.documentId} w="100%">
              <Grid.Col span={12}>
                <ImageVideoPreview
                  key={rec.documentId}
                  href={`/${rec.follower?.type}/${
                    rec.follower?.username
                  }/live/${rec.documentId}${sort ? `?sort=` + sort : null}`}
                  type={rec.follower?.type}
                  sources={rec.sources}
                />
              </Grid.Col>
              <Grid.Col span={12}>
                <Group gap="xs">
                  <Anchor
                    component={Link}
                    href={`/${rec.follower?.type}/${rec.follower?.username}`}
                  >
                    <Avatar
                      size={38}
                      src={rec.follower?.avatar?.url}
                      styles={{
                        image: {
                          transform: "scale(2)",
                          objectFit: "cover",
                        },
                      }}
                    />
                  </Anchor>

                  <Stack gap="0">
                    <Anchor
                      component={Link}
                      href={`/${rec.follower?.type}/${rec.follower?.username}`}
                      size="md"
                      truncate
                      maw={110}
                      display="inline-block"
                    >
                      {rec.follower?.username}
                    </Anchor>
                    <Text size="xs">live {dayjs(rec.createdAt).fromNow()}</Text>
                  </Stack>
                </Group>
              </Grid.Col>
            </Grid>
          );
        })}
      </SimpleGrid>
      {/* Sentinel element */}
      <div ref={ref} style={{ height: 1 }} />

      {isPending && <Loader size="sm" style={{ alignSelf: "center" }} />}

      {!hasMore && data && data.length > 0 && (
        <p style={{ textAlign: "center", color: "gray" }}>No more to load</p>
      )}
    </Stack>
  );
}
