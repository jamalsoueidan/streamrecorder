import { getRecordings } from "@/app/actions/recordings";
import dayjs from "@/app/lib/dayjs";
import api from "@/lib/api";
import { SortOptions } from "@/lib/types/filtering";
import { Follower } from "@/lib/types/strapi-autogenerated";
import {
  ActionIcon,
  Anchor,
  Avatar,
  Box,
  Button,
  Group,
  Stack,
  Text,
  Title,
} from "@mantine/core";
import { IconVideo, IconWorldSearch } from "@tabler/icons-react";
import { CountryFlag } from "../../_components/country-flag";
import FollowButton from "../../_components/follow-button";
import { FollowerTypeIcon } from "../../_components/follower-type";
import InfiniteRecordings from "../../_components/infinity-recordings";
import OpenSocial from "../../_components/open-social";
import UnfollowButton from "../../_components/unfollow-button";

interface PageProps {
  params: Promise<{
    username: string;
    type: string;
  }>;
}

export default async function Page({ params }: PageProps) {
  const { type, username } = await params;

  const response = await api.follower.getFollowers({
    filters: {
      username,
      type,
    },
    populate: ["avatar"],
  });

  const follower = response.data.data?.at(0);

  if (!follower) {
    return <>Not found</>;
  }

  const user =
    await api.usersPermissionsUsersRoles.getUsersPermissionsUsersRoles({
      populate: {
        followers: {
          fields: ["id"],
          filters: {
            documentId: {
              $eq: follower?.documentId,
            },
          },
        },
      },
    });

  const followers: Follower[] = (user.data as any).followers;
  const isFollowing = followers.find((f) => f.id === follower.id);
  const { data, meta } = await getRecordings({
    filters: {
      follower: {
        documentId: {
          $eq: follower?.documentId,
        },
      },
    },
    "pagination[page]": 1,
    sort: SortOptions.createdAtDesc,
  });

  const fetchAction = async (options: Parameters<typeof getRecordings>[0]) => {
    "use server";
    delete options.scope; //remove scope, incase user does not follow streamer
    return await getRecordings({
      ...options,
      filters: {
        follower: {
          documentId: {
            $eq: follower?.documentId,
          },
        },
      },
    });
  };

  return (
    <section>
      <Group mb="xl">
        <Box pos="relative">
          <Avatar
            size={150}
            src={follower.avatar?.url}
            styles={{
              image: {
                transform: "scale(2)",
                objectFit: "cover",
              },
            }}
          />

          {follower.type ? (
            <>
              <FollowerTypeIcon
                pos="absolute"
                color="transparent"
                type={follower?.type}
                top="50%"
                left="50%"
                size={90}
                style={{ transform: "translate(-50%, -50%)" }}
              />
            </>
          ) : null}
        </Box>

        <Stack gap="xs">
          <Group>
            <Title order={2}>{follower.username}</Title>
            {follower.country ? (
              <CountryFlag country={follower?.country} size={40} />
            ) : null}
          </Group>

          <Group gap="xs">
            <OpenSocial follower={follower} />
            {isFollowing ? (
              <>
                <UnfollowButton documentId={follower.documentId!} />
              </>
            ) : (
              <FollowButton
                username={follower.username!}
                type={follower.type}
              />
            )}
          </Group>
          <div>
            <Text size="sm" c="dimmed">
              added {dayjs(follower.createdAt).fromNow()}
            </Text>
            <Text size="sm" c="dimmed">
              last checked {dayjs(follower.lastCheckedAt).fromNow()}
            </Text>
          </div>
        </Stack>
      </Group>

      {data.length === 0 ? (
        <Stack align="center" justify="center" py={80} gap="lg">
          <ActionIcon
            variant="transparent"
            size={120}
            radius="xl"
            color="white"
          >
            <IconVideo size={90} stroke={2} />
          </ActionIcon>
          <Stack align="center" gap={12}>
            <Title order={2} fw={600}>
              No recordings yet
            </Title>
            <Text size="xl" c="dimmed" maw={450} ta="center">
              Follow streamers to see their recordings here
            </Text>
          </Stack>
          <Anchor href="/discover" underline="never">
            <Button
              size="lg"
              radius="md"
              leftSection={<IconWorldSearch size={20} />}
            >
              Discover Streamers
            </Button>
          </Anchor>
        </Stack>
      ) : (
        <InfiniteRecordings
          initialData={data}
          initialPagination={meta}
          initialSort={SortOptions.createdAtDesc}
          fetchAction={fetchAction}
        />
      )}
    </section>
  );
}
