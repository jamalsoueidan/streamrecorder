import publicApi from "@/lib/public-api";
import { Clip } from "@/lib/types/strapi-autogenerated";
import { GetObjectCommand, S3Client } from "@aws-sdk/client-s3";

const s3 = new S3Client({
  region: "fsn1",
  endpoint: "https://fsn1.your-objectstorage.com",
  credentials: {
    accessKeyId: process.env.S3_ACCESS_KEY!,
    secretAccessKey: process.env.S3_SECRET_KEY!,
  },
});

export async function GET(
  request: Request,
  { params }: { params: Promise<{ documentId: string }> },
) {
  const { documentId } = await params;

  const { data } = await publicApi.clip.getClipsId({
    id: documentId,
  });

  if (!data.data) {
    return new Response("Not found", { status: 404 });
  }

  const clip = data.data as unknown as Clip;
  const abortController = new AbortController();

  try {
    const command = new GetObjectCommand({
      Bucket: process.env.CLIP_BUCKET!,
      Key: `${clip.path?.substring(1)}${documentId}/thumbnail.jpg`,
    });
    const response = await s3.send(command, {
      abortSignal: abortController.signal,
    });

    return new Response(response.Body as ReadableStream, {
      headers: {
        "Content-Type": "image/jpeg",
        "Content-Length": response.ContentLength?.toString() || "",
        "Cache-Control": "public, max-age=31536000",
      },
    });
  } catch (error: any) {
    abortController.abort();

    if (error.Code === "NoSuchKey" || error.name === "NoSuchKey") {
      return new Response("Not found", { status: 404 });
    }

    console.error("Error fetching preview:", error);
    return new Response("Error", { status: 500 });
  }
}
