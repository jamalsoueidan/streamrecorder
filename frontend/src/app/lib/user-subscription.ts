import "server-only";

import publicApi from "@/lib/public-api";
import {
  PaymentProviderEnum,
  SubscriptionStatusEnum,
} from "@/lib/types/strapi-autogenerated";

// Get role ID by name from Strapi
export async function getRoleIdByName(name: string): Promise<number | null> {
  try {
    const response = await publicApi.usersPermissionsUsersRoles.rolesList();
    const roles = response.data?.roles;
    const role = roles?.find(
      (r: { name?: string }) => r.name?.toLowerCase() === name.toLowerCase(),
    );
    return role?.id || null;
  } catch (error) {
    console.error("Failed to fetch role:", error);
    return null;
  }
}

// Find user by email
export async function findUserByEmail(email: string) {
  try {
    const response = await publicApi.usersPermissionsUsersRoles.usersList({
      query: {
        filters: {
          email: {
            $eqi: email,
          },
        },
      },
    } as never);

    const users = response.data;
    return users[0] || null;
  } catch (error) {
    console.error("Failed to find user by email:", error);
    return null;
  }
}

// Update user subscription data — provider-agnostic. paymentProvider intentionally
// excludes "freemius" (Freemius integration removed 2026-05-27); legacy freemius
// rows in the DB still resolve correctly because the column is just a string enum.
export interface SubscriptionUpdate {
  role?: number;
  subscriptionStatus?: SubscriptionStatusEnum;
  subscriptionEndDate?: string | null;
  billingPeriod?: string | null;
  paymentProvider?: Exclude<PaymentProviderEnum, PaymentProviderEnum.Freemius>;
  trialClaimed?: boolean;
}

export async function updateUserSubscription(
  strapiUserId: string,
  data: SubscriptionUpdate,
) {
  try {
    const response = await publicApi.usersPermissionsUsersRoles.usersUpdate(
      { id: strapiUserId },
      data as never,
    );
    return response.data;
  } catch (error) {
    console.error("Failed to update user:", error);
    return null;
  }
}
