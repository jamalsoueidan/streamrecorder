import { Follower, FollowerTypeEnum } from "@/lib/types/strapi-autogenerated";
import { getProfileUrl } from "../(protected)/components/open-social";

export function generateProfileUrl(
  follower?: Partial<Pick<Follower, "username" | "type">>,
  fullURL: boolean = false
) {
  if (!follower) {
    return "";
  }
  if (follower.type === FollowerTypeEnum.Tiktok) {
    return (
      (fullURL ? `https://livestreamrecorder.com` : ``) +
      getProfileUrl(follower)
    );
  }
  return (
    (fullURL ? `https://livestreamrecorder.com` : ``) + getProfileUrl(follower)
  );
}
