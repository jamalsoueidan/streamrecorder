// DASH manifest for the same fmp4 segments served by playlist.m3u8.
// The ONLY consumer of this route is the dash.js + @hevcjs/dashjs-plugin
// test page, which lets browsers without native HEVC decode (Chrome on
// Win/Linux) play TikTok recordings via client-side HEVC→H.264 wasm
// transcode.
//
// Multi-source recordings get one <Period> per source. SegmentList +
// mediaRange (per HLS EXT-X-BYTERANGE) avoids needing dash.js to parse
// the SIDX boxes, which can be ambiguous when video+audio each have
// their own SIDX.

const VIEW_SESSION_COOKIE = "view_session";

interface SegmentRef {
  duration: number; // seconds
  start: number; // byte offset
  length: number; // byte length
}

interface SourceManifest {
  baseUrl: string;
  initRange: { start: number; length: number };
  segments: SegmentRef[];
  totalDuration: number;
  codecs: string;
}

// Probe the mp4 init segment for the actual codec string. Reads the
// `avcC` (H.264 decoder config) or `hvcC` (HEVC decoder config) box
// right after its anchor and extracts the exact profile/level/etc bytes
// that MUST match the codec string in the DASH manifest — Chromium
// strict-checks "Video stream codec X doesn't match SourceBuffer codecs"
// otherwise.
//
// avcC layout: [4 size][4 'avcC'][1 cfgVer][1 profile][1 compat][1 level]…
// hvcC layout: [4 size][4 'hvcC'][1 cfgVer][1 profile_space|tier|profile_idc]
//              [4 compat_flags][6 constraint_flags][1 level_idc]…
async function detectCodecsFromInit(
  s3Client: S3Client,
  bucket: string,
  key: string,
  initStart: number,
  initLength: number,
): Promise<string> {
  const fallback = "avc1.640028,mp4a.40.2";
  const hex2 = (n: number) => n.toString(16).padStart(2, "0").toUpperCase();
  try {
    const command = new GetObjectCommand({
      Bucket: bucket,
      Key: key,
      Range: `bytes=${initStart}-${initStart + initLength - 1}`,
    });
    const response = await s3Client.send(command);
    if (!response.Body) return fallback;
    const buf = await response.Body.transformToByteArray();
    const view = buf instanceof Uint8Array ? buf : new Uint8Array(buf);
    const text = new TextDecoder("latin1").decode(view);

    // Find the sample entry FourCC (avc1/avc3/hvc1/hev1) — it lives at
    // offset 16 from each `stsd` anchor.
    const sampleEntryFourCC = (() => {
      let i = text.indexOf("stsd");
      while (i !== -1) {
        const f = text.slice(i + 16, i + 20);
        if (f === "hvc1" || f === "hev1" || f === "avc1" || f === "avc3") {
          return f;
        }
        i = text.indexOf("stsd", i + 4);
      }
      return null;
    })();

    let result = fallback;
    // text.indexOf("avcC"/"hvcC") returns the offset of the *type* field.
    // The 4-byte box size is BEFORE that, and the config record payload
    // starts AFTER the type field — so payload begins at `idx + 4`.
    if (sampleEntryFourCC === "hvc1" || sampleEntryFourCC === "hev1") {
      const hvccIdx = text.indexOf("hvcC");
      if (hvccIdx !== -1) {
        const o = hvccIdx + 4; // payload start
        const cfg = view[o + 1];
        const profileSpace = (cfg >> 6) & 0x3;
        const tierFlag = (cfg >> 5) & 0x1;
        const profileIdc = cfg & 0x1f;
        const compat =
          (view[o + 2] << 24) |
          (view[o + 3] << 16) |
          (view[o + 4] << 8) |
          view[o + 5];
        const constraint = Array.from(view.slice(o + 6, o + 12))
          .map((b) => hex2(b))
          .join("");
        const levelIdc = view[o + 12];
        const profilePrefix = ["", "A", "B", "C"][profileSpace] || "";
        result =
          `${sampleEntryFourCC}.${profilePrefix}${profileIdc}` +
          `.${(compat >>> 0).toString(16).toUpperCase()}` +
          `.${tierFlag ? "H" : "L"}${levelIdc}` +
          `.${constraint.replace(/0+$/, "") || "B0"},mp4a.40.2`;
      }
    }
    if (sampleEntryFourCC === "avc1" || sampleEntryFourCC === "avc3") {
      const avccIdx = text.indexOf("avcC");
      if (avccIdx !== -1) {
        const o = avccIdx + 4; // payload start
        const profile = view[o + 1];
        const compat = view[o + 2];
        const level = view[o + 3];
        result = `${sampleEntryFourCC}.${hex2(profile)}${hex2(compat)}${hex2(level)},mp4a.40.2`;
      }
    }
    console.log(
      `[manifest.mpd] probed key=${key} fourCC=${sampleEntryFourCC} → codecs="${result}"`,
    );
    return result;
  } catch {
    return fallback;
  }
}

// Cache disabled — see comment on buildDashManifest.
async function fetchSourcePlaylists(documentId: string) {
  return _fetchSourcePlaylistsImpl(documentId);
}
async function _fetchSourcePlaylistsImpl(documentId: string) {
    const response = await publicApi.source.getSources({
      filters: {
        recording: { documentId },
        state: { $eq: "done" },
      },
      sort: "createdAt:asc",
      fields: ["path", "createdAt", "bucket", "endpoint"],
      // eslint-disable-next-line @typescript-eslint/no-explicit-any
    } as any);

    const sources = response.data.data ?? [];
    if (!sources.length) return null;

    const sourcesWithPlaylists = await fetchPlaylistsFromS3(sources);

    return { sourcesWithPlaylists };
}

// Cache disabled while debugging codec detection. The probe runs every
// request; re-enable unstable_cache once codec strings are correct.
async function buildDashManifest(documentId: string) {
  const cached = await fetchSourcePlaylists(documentId);
  if (!cached) return null;
  const sourceManifests = await buildSourceManifests(
    cached.sourcesWithPlaylists,
  );
  return renderMpd(sourceManifests);
}

export async function GET(
  _request: NextRequest,
  { params }: { params: Promise<{ documentId: string }> },
) {
  const { documentId } = await params;

  const cookieStore = await cookies();
  const token = cookieStore.get(VIEW_SESSION_COOKIE)?.value;
  if (!verifyViewToken(token)) {
    return new Response("Forbidden", { status: 403 });
  }

  const mpd = await buildDashManifest(documentId);
  if (!mpd) return new Response("Not found", { status: 404 });

  return new Response(mpd, {
    headers: {
      "Content-Type": "application/dash+xml",
      "Cache-Control": "private, max-age=60",
    },
  });
}

type SourceWithPlaylist = Source & { playlist?: string | null };

async function fetchFromS3(
  s3Client: S3Client,
  bucket: string,
  key: string,
): Promise<string | null> {
  try {
    const command = new GetObjectCommand({ Bucket: bucket, Key: key });
    const response = await s3Client.send(command);
    if (!response.Body) return null;
    return await response.Body.transformToString();
  } catch (error: unknown) {
    const e = error as { Code?: string; name?: string };
    if (e.Code !== "NoSuchKey" && e.name !== "NoSuchKey") {
      console.error("Error fetching playlist from S3", { key }, error);
    }
    return null;
  }
}

// Per-source S3 client / bucket — sources can live on different
// backends (Hetzner / Backblaze) and signing them all with one client
// breaks playback the moment a recording is split across providers.
async function fetchPlaylistsFromS3(
  sources: Source[],
): Promise<SourceWithPlaylist[]> {
  const playlists = await Promise.all(
    sources.map((source) => {
      if (!source.path) return Promise.resolve(null);
      const client = getS3(
        source.endpoint,
      );
      const bucket = getBucket(process.env.MEDIA_BUCKET!, source.bucket);
      return fetchFromS3(
        client,
        bucket,
        `${source.path.substring(1)}playlist.m3u8`,
      );
    }),
  );
  return sources.map((source, i) => ({ ...source, playlist: playlists[i] }));
}

// Parse one HLS playlist into a structured per-source manifest. Each
// source has a single video.mp4 + a series of byte-range segments.
function parseSourcePlaylist(
  playlist: string,
  signedMp4Url: string,
  codecs: string,
): SourceManifest | null {
  const lines = playlist.split("\n");
  let initStart = 0;
  let initLength = 0;
  const segments: SegmentRef[] = [];
  let pendingDuration = 0;
  let pendingByteRange: { length: number; start: number } | null = null;
  let lastEnd = 0;
  let totalDuration = 0;

  for (const line of lines) {
    const trimmed = line.trim();

    if (trimmed.startsWith("#EXT-X-MAP:")) {
      const m = trimmed.match(/BYTERANGE="(\d+)@(\d+)"/);
      if (m) {
        initLength = parseInt(m[1], 10);
        initStart = parseInt(m[2], 10);
      }
      continue;
    }

    if (trimmed.startsWith("#EXTINF:")) {
      const m = trimmed.match(/#EXTINF:([\d.]+)/);
      if (m) pendingDuration = parseFloat(m[1]);
      continue;
    }

    if (trimmed.startsWith("#EXT-X-BYTERANGE:")) {
      const m = trimmed.match(/#EXT-X-BYTERANGE:(\d+)(?:@(\d+))?/);
      if (m) {
        const length = parseInt(m[1], 10);
        const start = m[2] !== undefined ? parseInt(m[2], 10) : lastEnd;
        pendingByteRange = { length, start };
      }
      continue;
    }

    if (
      trimmed &&
      !trimmed.startsWith("#") &&
      pendingByteRange &&
      pendingDuration > 0
    ) {
      segments.push({
        duration: pendingDuration,
        start: pendingByteRange.start,
        length: pendingByteRange.length,
      });
      lastEnd = pendingByteRange.start + pendingByteRange.length;
      totalDuration += pendingDuration;
      pendingByteRange = null;
      pendingDuration = 0;
    }
  }

  if (segments.length === 0) return null;

  return {
    baseUrl: signedMp4Url,
    initRange: { start: initStart, length: initLength },
    segments,
    totalDuration,
    codecs,
  };
}

async function buildSourceManifests(
  sources: SourceWithPlaylist[],
): Promise<SourceManifest[]> {
  const manifests: SourceManifest[] = [];

  for (const source of sources) {
    if (!source.playlist || !source.path) continue;

    // Each source has its own backend; derive the client + bucket here
    // so a recording that spans providers still produces a valid MPD.
    const s3Client = getS3(
      source.endpoint,
    );
    const bucket = getBucket(process.env.MEDIA_BUCKET!, source.bucket);

    // Find the video.mp4 referenced in this source's playlist (always
    // a single file per source — segments are byte-range refs into it).
    const mapMatch = source.playlist.match(/#EXT-X-MAP:URI="([^"]+)"/);
    if (!mapMatch) continue;
    const filename = mapMatch[1];

    const s3Key = `${source.path.substring(1)}${filename}`;
    const command = new GetObjectCommand({ Bucket: bucket, Key: s3Key });
    const signedUrl = await getSignedUrl(s3Client, command, {
      expiresIn: 14400,
    });
    const baseUrl = proxySignedUrl(signedUrl);

    // Parse the playlist first so we know the init segment range; then
    // probe those bytes from S3 for the actual codec FourCC.
    const partial = parseSourcePlaylist(source.playlist, baseUrl, "");
    if (!partial) continue;
    const codecs = await detectCodecsFromInit(
      s3Client,
      bucket,
      s3Key,
      partial.initRange.start,
      partial.initRange.length,
    );
    manifests.push({ ...partial, codecs });
  }

  return manifests;
}

function pt(seconds: number): string {
  // ISO 8601 duration, second precision is enough for our use
  return `PT${seconds.toFixed(3)}S`;
}

function escapeXml(s: string): string {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function renderMpd(sources: SourceManifest[]): string {
  if (sources.length === 0) return "";

  const totalDuration = sources.reduce((acc, s) => acc + s.totalDuration, 0);

  // Each source becomes its own Period. dash.js handles the cross-Period
  // boundaries (acts like an EXT-X-DISCONTINUITY in HLS).
  const periods = sources
    .map((source, idx) => {
      const segUrls = source.segments
        .map(
          (seg) =>
            `        <SegmentURL mediaRange="${seg.start}-${seg.start + seg.length - 1}"/>`,
        )
        .join("\n");
      const timelineEntries = source.segments
        .map((seg) => `          <S d="${Math.round(seg.duration * 1000)}"/>`)
        .join("\n");

      return `  <Period id="p${idx}" duration="${pt(source.totalDuration)}">
    <AdaptationSet contentType="video" mimeType="video/mp4" segmentAlignment="true">
      <Representation id="r${idx}" codecs="${source.codecs}" bandwidth="2000000">
        <BaseURL>${escapeXml(source.baseUrl)}</BaseURL>
        <SegmentList timescale="1000">
          <Initialization range="${source.initRange.start}-${source.initRange.start + source.initRange.length - 1}"/>
          <SegmentTimeline>
${timelineEntries}
          </SegmentTimeline>
${segUrls}
        </SegmentList>
      </Representation>
    </AdaptationSet>
  </Period>`;
    })
    .join("\n");

  return `<?xml version="1.0" encoding="UTF-8"?>
<MPD xmlns="urn:mpeg:dash:schema:mpd:2011"
     type="static"
     mediaPresentationDuration="${pt(totalDuration)}"
     minBufferTime="PT2S"
     profiles="urn:mpeg:dash:profile:isoff-on-demand:2011">
${periods}
</MPD>
`;
}
