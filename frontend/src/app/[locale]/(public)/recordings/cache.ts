"use cache";

import publicApi from "@/lib/public-api";
import { FollowerTypeEnum } from "@/lib/types/strapi-autogenerated";
import { cacheLife } from "next/cache";

export async function getFollowerFilters(type?: FollowerTypeEnum) {
  cacheLife({ stale: 300, revalidate: 1800, expire: 3600 });

  const { data: filtersData } = await publicApi.follower.getFollowerFilters(
    type ? { type } : { type: undefined },
  );

  return {
    countryCodes: (filtersData.countryCodes ?? [])
      .filter((c) => c.value !== "-")
      .map((c) => ({
        value: c.value || "",
        label: `${c.value} (${c.count})`,
      })),
  };
}

export async function getRecordings(type: string, page: number) {
  cacheLife({ stale: 300, revalidate: 1800, expire: 3600 });

  const {
    data: { data: recordings, meta },
  } = await publicApi.recording.getRecordings({
    filters: {
      ...(type === "all"
        ? {}
        : {
            follower: {
              type,
            },
          }),
      sources: {
        state: {
          $eq: ["done"],
        },
      },
    },
    "pagination[pageSize]": 20,
    "pagination[page]": page,
    sort: "createdAt:desc",
    populate: {
      sources: {
        fields: ["*"],
        filters: {
          state: {
            $eq: "done",
          },
        },
      },
      follower: {
        fields: ["*"],
      },
    },
  });

  return { recordings: recordings ?? [], meta };
}

export async function getRecordingsByCountry(
  type: string,
  countryCode: string,
  page: number,
) {
  cacheLife({ stale: 300, revalidate: 1800, expire: 3600 });

  const hasPlatform = type !== "all";

  const {
    data: { data: recordings, meta },
  } = await publicApi.recording.getRecordings({
    filters: {
      follower: {
        ...(hasPlatform ? { type } : {}),
        countryCode,
      },
      sources: {
        state: {
          $eq: ["done"],
        },
      },
    },
    "pagination[pageSize]": 20,
    "pagination[page]": page,
    sort: "createdAt:desc",
    populate: {
      sources: {
        fields: ["*"],
        filters: {
          state: {
            $eq: "done",
          },
        },
      },
      follower: {
        fields: ["*"],
      },
    },
  });

  return { recordings: recordings ?? [], meta };
}
