import { Clip } from "@/lib/types/strapi-autogenerated";
import { ScrollArea } from "@mantine/core";

export function ClipSlider({ clips }: { clips: Clip[] }) {
  return (
    <div style={{ overflow: "hidden" }}>
      <ScrollArea type="never" scrollbarSize={0} mt="xl" mx={-32}>
        <div
          style={{
            display: "flex",
            gap: "20px",
            justifyContent: "center",
            minWidth: "max-content",
          }}
        >
          {clips.map((clip) => (
            <video
              key={clip.id}
              poster={`/clip/${clip.documentId}/thumbnail.jpg`}
              preload="metadata"
              autoPlay
              muted
              loop
              playsInline
              height={400}
              style={{
                flexShrink: 0,
                pointerEvents: "none",
                borderRadius: "8px",
              }}
            >
              <source
                src={`/clip/${clip.documentId}/preview.mp4`}
                type="video/mp4"
              />
            </video>
          ))}
        </div>
      </ScrollArea>
    </div>
  );
}
