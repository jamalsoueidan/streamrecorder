import { FollowerTypeIcon } from "@/app/[locale]/my/components/follower-type-icon";
import { getClipUrl } from "@/app/lib/clip-url";
import { getSignedClipPlayUrl } from "@/app/lib/clip-url.server";

import { Clip, FollowerTypeEnum } from "@/lib/types/strapi-autogenerated";
import { ClipPlayer } from "./clip-player";

interface Props {
  clip: Clip;
  type: FollowerTypeEnum;
  locale: string;
}

export async function ImageClipPreview({ clip, type, locale }: Props) {
  const signedClipUrl = await getSignedClipPlayUrl(clip);
  return (
    <div
      style={{
        position: "relative",
        overflow: "hidden",
      }}
    >
      <ClipPlayer
        previewUrl={getClipUrl(clip.documentId!, "thumbnail.jpg", clip.path)}
        src={signedClipUrl || getClipUrl(clip.documentId!, "clip.mp4", clip.path)}
        subtitlesUrl={`/clip/${clip.documentId}/subtitles.vtt?locale=${locale}`}
        locale={locale}
      />

      <FollowerTypeIcon
        type={type}
        color="black"
        opacity={0.8}
        pos="absolute"
        top={20}
        right={18}
        style={{
          pointerEvents: "none",
        }}
      />
    </div>
  );
}
