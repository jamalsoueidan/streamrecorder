import RecordingItem from "@/app/[locale]/(protected)/components/recording-item";
import { FollowerTypeEnum, Recording } from "@/lib/types/strapi-autogenerated";
import {
  Box,
  Button,
  Divider,
  Flex,
  Group,
  Image,
  SimpleGrid,
  Stack,
  Text,
  Title,
} from "@mantine/core";
import { IconChevronRight, IconVideo } from "@tabler/icons-react";
import { getFormatter, getTranslations } from "next-intl/server";
import Link from "@/app/components/link";

interface PlatformWithRecordings {
  name: string;
  file: string;
  color: string;
  colorCss: string;
  type: FollowerTypeEnum;
  recordings: Recording[];
}

interface MyFeedSectionProps {
  myFeedPromise: Promise<PlatformWithRecordings[]>;
}

export async function MyFeedSection({ myFeedPromise }: MyFeedSectionProps) {
  const t = await getTranslations("protected.dashboard");
  const format = await getFormatter();
  const myFeed = await myFeedPromise;

  if (!myFeed.some((p) => p.recordings.length > 0)) {
    return null;
  }

  return (
    <>
      <Divider />
      <Box
        style={{
          background: "linear-gradient(150deg, #BDFF88, #5ABB91)",
        }}
        py="sm"
        px={{ base: "xs", sm: "md" }}
      >
        <Flex justify="space-between" align="center" w="100%">
          <Stack gap={2}>
            <Flex gap="xs" align="center">
              <IconVideo size={24} color="black" />
              <Title order={1} size="lg" c="black">
                {t("myFeed")}
              </Title>
            </Flex>
          </Stack>
        </Flex>
      </Box>
      <Divider />

      {myFeed.map((platform) =>
        platform.recordings.length > 0 ? (
          <Stack key={platform.name} gap="md" p="md">
            <Flex justify="space-between" align="center">
              <Flex gap="xs" align="center">
                <Image
                  src={platform.file}
                  alt={platform.name}
                  w={24}
                  h={24}
                  style={{ filter: "brightness(0) invert(1)" }}
                />
                <Title order={2} size="h4">
                  {platform.name}
                </Title>
              </Flex>
              <Link href={`/following?type=${platform.type}`}>
                <Button
                  variant="outline"
                  color="gray"
                  size="sm"
                  rightSection={<IconChevronRight size={16} />}
                >
                  {t("browseAll")}
                </Button>
              </Link>
            </Flex>

            <SimpleGrid cols={{ base: 2, sm: 2, md: 4 }} spacing="md">
              {platform.recordings.map((rec) => (
                <RecordingItem key={rec.documentId} recording={rec} />
              ))}
            </SimpleGrid>
          </Stack>
        ) : null,
      )}
    </>
  );
}
