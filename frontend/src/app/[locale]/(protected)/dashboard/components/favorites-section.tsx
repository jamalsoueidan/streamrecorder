import RecordingItem from "@/app/[locale]/(protected)/components/recording-item";
import Link from "@/app/components/link";
import { Recording } from "@/lib/types/strapi-autogenerated";
import {
  Box,
  Button,
  Divider,
  Flex,
  SimpleGrid,
  Stack,
  Title,
} from "@mantine/core";
import { IconChevronRight, IconStar } from "@tabler/icons-react";
import { getTranslations } from "next-intl/server";

interface FavoritesSectionProps {
  favoritesPromise: Promise<Recording[]>;
}

export async function FavoritesSection({
  favoritesPromise,
}: FavoritesSectionProps) {
  const t = await getTranslations("protected.dashboard");
  const favorites = await favoritesPromise;

  if (!favorites.length) return null;

  return (
    <>
      <Divider />
      <Box
        style={{
          background: "linear-gradient(150deg, #FFF4B8, #FFD700)",
        }}
        py="sm"
        px={{ base: "xs", sm: "md" }}
      >
        <Flex justify="space-between" align="center" w="100%">
          <Flex gap="xs" align="center">
            <IconStar size={24} color="black" />
            <Title order={1} size="lg" c="black">
              {t("favorites")}
            </Title>
          </Flex>
          <Link href="/following?favorites=true">
            <Button
              variant="transparent"
              color="black"
              size="compact-sm"
              rightSection={<IconChevronRight size={16} />}
            >
              {t("browseAll")}
            </Button>
          </Link>
        </Flex>
      </Box>
      <Divider />

      <Stack gap="md" p="md">
        <SimpleGrid cols={{ base: 2, sm: 2, md: 4 }} spacing="md">
          {favorites.map((rec) => (
            <RecordingItem key={rec.documentId} recording={rec} />
          ))}
        </SimpleGrid>
      </Stack>
    </>
  );
}
