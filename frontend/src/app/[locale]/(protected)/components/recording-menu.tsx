"use client";

import { downloadRecording } from "@/app/actions/download-recording";
import {
  deleteRecording,
  deleteRecordingSources,
  toggleHidden,
} from "@/app/actions/recording";
import { useWatchLater } from "@/app/hooks/use-watch-later";
import { useUser } from "@/app/providers/user-provider";
import { Recording } from "@/lib/types/strapi-autogenerated";
import { ActionIcon, Menu, ScrollArea, Text } from "@mantine/core";
import { modals } from "@mantine/modals";
import { notifications } from "@mantine/notifications";
import {
  IconBookmark,
  IconBookmarkFilled,
  IconDatabaseX,
  IconDots,
  IconDownload,
  IconEye,
  IconEyeOff,
  IconMail,
  IconScissors,
  IconSparkles,
  IconTrash,
} from "@tabler/icons-react";
import { useQueryClient } from "@tanstack/react-query";
import { useLocale, useTranslations } from "next-intl";
import Link from "@/app/components/link";
import { useState } from "react";
import { DownloadUpgradeModal } from "./download-upgrade-modal";
import { VideoEditorModal } from "./video-editor-modal";

interface RecordingMenuProps {
  recording: Recording;
  username?: string;
  type?: string;
}

function formatDuration(seconds?: number | null): string {
  if (!seconds) return "0:00";
  const h = Math.floor(seconds / 3600);
  const m = Math.floor((seconds % 3600) / 60);
  const s = Math.floor(seconds % 60);
  if (h > 0) {
    return `${h}:${m.toString().padStart(2, "0")}:${s.toString().padStart(2, "0")}`;
  }
  return `${m}:${s.toString().padStart(2, "0")}`;
}

export function RecordingMenu({
  recording,
  username,
  type,
}: RecordingMenuProps) {
  const user = useUser();
  const t = useTranslations("protected.common.recordings");
  const queryClient = useQueryClient();
  const { isInWatchLater, toggleWatchLater } = useWatchLater();
  const locale = useLocale();
  const [downloadUpgradeOpened, setDownloadUpgradeOpened] = useState(false);
  const [editorOpened, setEditorOpened] = useState(false);

  const isOwner = user?.id && recording.follower?.owner?.id === user.id;
  const isFollowing = user?.followers?.some(
    (f) => f.documentId === recording.follower?.documentId,
  );
  const isPremium = user?.role?.type !== "authenticated";

  const inWatchLater = recording.documentId
    ? isInWatchLater(recording.documentId)
    : false;

  const handleDelete = () => {
    if (!recording.documentId) return;
    modals.openConfirmModal({
      title: t("deleteConfirm.title"),
      children: <Text size="sm">{t("deleteConfirm.message")}</Text>,
      labels: {
        confirm: t("deleteConfirm.confirm"),
        cancel: t("deleteConfirm.cancel"),
      },
      confirmProps: { color: "red" },
      onConfirm: async () => {
        const result = await deleteRecording(recording.documentId!);
        if (result.success && username && type) {
          queryClient.invalidateQueries({
            queryKey: ["recordings", username, type],
          });
        }
      },
    });
  };

  const handleDeleteSources = async () => {
    if (!recording.documentId) return;
    const result = await deleteRecordingSources(recording.documentId);
    if (result.success) {
      notifications.show({
        title: t("deleteSourcesSuccess", { count: result.deletedCount || 0 }),
        message: null,
        color: "green",
      });
      if (username && type) {
        queryClient.invalidateQueries({
          queryKey: ["recordings", username, type],
        });
      }
    }
  };

  const handleToggleHidden = async () => {
    if (!recording.documentId) return;
    const result = await toggleHidden(recording.documentId, !!recording.hidden);
    if (result.success && username && type) {
      queryClient.invalidateQueries({
        queryKey: ["recordings", username, type],
      });
    }
  };

  const handleToggleWatchLater = () => {
    if (!recording.documentId) return;
    const wasInWatchLater = inWatchLater;
    toggleWatchLater(recording.documentId);
    notifications.show({
      title: wasInWatchLater
        ? t("removedFromWatchLater")
        : t("addedToWatchLater"),
      message: null,
      color: wasInWatchLater ? "gray" : "green",
    });
  };

  const handleDownload = async () => {
    if (!recording.documentId || !user?.id) return;
    if (!isPremium) {
      setDownloadUpgradeOpened(true);
      return;
    }
    await downloadRecording(recording.documentId, user.id, locale);
    notifications.show({
      title: t("downloadEmailSent"),
      message: null,
      color: "green",
    });
  };

  const handleDownloadPart = (sourceDocumentId: string) => {
    if (!recording.documentId) return;
    if (!isPremium) {
      setDownloadUpgradeOpened(true);
      return;
    }
    window.open(
      `/video/${recording.documentId}/video.mp4?sourceId=${sourceDocumentId}&download`,
      "_blank",
    );
  };

  return (
    <>
      <Menu shadow="md">
        <Menu.Target>
          <ActionIcon
            variant="filled"
            color="dark"
            size="md"
            onClick={(e) => e.preventDefault()}
          >
            <IconDots />
          </ActionIcon>
        </Menu.Target>
        <Menu.Dropdown>
          <Menu.Item
            leftSection={
              inWatchLater ? <IconBookmarkFilled /> : <IconBookmark />
            }
            onClick={handleToggleWatchLater}
          >
            {inWatchLater ? t("removeFromWatchLater") : t("addToWatchLater")}
          </Menu.Item>
          {isFollowing && (
            <Menu.Item
              component={Link}
              href={`/ai-studio/create/${recording.documentId}`}
              leftSection={<IconSparkles />}
            >
              {t("createWithAI")}
            </Menu.Item>
          )}
          <Menu.Item
            leftSection={<IconScissors />}
            onClick={() => setEditorOpened(true)}
          >
            {t("editClip")}
          </Menu.Item>
          <Menu.Divider />
          <Menu.Label>{t("download")}</Menu.Label>
          <Menu.Item leftSection={<IconMail />} onClick={handleDownload}>
            {t("sendToEmail")}
          </Menu.Item>
          <ScrollArea.Autosize mah={200} scrollbarSize={6} type="always">
            {recording.sources && recording.sources.length > 1 ? (
              <>
                {recording.sources.map((source, index) => {
                  const startSeconds = recording
                    .sources!.slice(0, index)
                    .reduce((sum, s) => sum + (s.duration || 0), 0);
                  const endSeconds = startSeconds + (source.duration || 0);
                  return (
                    <Menu.Item
                      key={source.documentId}
                      leftSection={<IconDownload />}
                      onClick={() => handleDownloadPart(source.documentId!)}
                    >
                      {t("downloadPart", { part: index + 1 })} (
                      {formatDuration(startSeconds)} -{" "}
                      {formatDuration(endSeconds)})
                    </Menu.Item>
                  );
                })}
              </>
            ) : (
              <Menu.Item
                leftSection={<IconDownload />}
                onClick={() =>
                  recording.sources?.[0]?.documentId &&
                  handleDownloadPart(recording.sources[0].documentId)
                }
              >
                {t("downloadPart", { part: 1 })} (
                {formatDuration(recording.sources?.[0]?.duration)})
              </Menu.Item>
            )}
          </ScrollArea.Autosize>
          {(isOwner || user?.role?.type === "admin") && <Menu.Divider />}
          {isOwner && (
            <Menu.Item
              leftSection={recording.hidden ? <IconEye /> : <IconEyeOff />}
              onClick={handleToggleHidden}
            >
              {recording.hidden ? t("show") : t("hide")}
            </Menu.Item>
          )}
          {isOwner && (
            <Menu.Item
              color="red"
              leftSection={<IconTrash />}
              onClick={handleDelete}
            >
              {t("delete")}
            </Menu.Item>
          )}
          {user?.role?.type === "admin" && (
            <Menu.Item
              color="red"
              leftSection={<IconDatabaseX />}
              onClick={handleDeleteSources}
            >
              {t("deleteSources")}
            </Menu.Item>
          )}
        </Menu.Dropdown>
      </Menu>
      <DownloadUpgradeModal
        opened={downloadUpgradeOpened}
        onClose={() => setDownloadUpgradeOpened(false)}
      />
      <VideoEditorModal
        recording={recording}
        opened={editorOpened}
        onClose={() => setEditorOpened(false)}
      />
    </>
  );
}
