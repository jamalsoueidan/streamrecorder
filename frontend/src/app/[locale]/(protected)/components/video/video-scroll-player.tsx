"use client";

import { generateAvatarUrl } from "@/app/lib/avatar-url";
import { Recording } from "@/lib/types/strapi-autogenerated";
import { getImageUrl } from "@/app/lib/media-url";
import {
  ActionIcon,
  Avatar,
  Box,
  Drawer,
  Flex,
  Group,
  Loader,
  ScrollArea,
  Stack,
  Text,
} from "@mantine/core";
import { useDisclosure, useMediaQuery } from "@mantine/hooks";
import {
  IconChevronDown,
  IconChevronUp,
  IconList,
  IconVideoOff,
  IconX,
} from "@tabler/icons-react";
import Image from "next/image";
import Link from "@/app/components/link";
import {
  useCallback,
  useEffect,
  useLayoutEffect,
  useMemo,
  useRef,
  useState,
} from "react";
import { getProfileUrl } from "../../../../components/open-social";
import { FollowerTypeIcon } from "../follower-type-icon";
import { formatDuration } from "./player-utils";
import { VideoLoadIndicator } from "./video-load-indicator";
import { VideoPlayer } from "./video-player";

interface VideoScrollPlayerProps {
  recordings: Recording[];
  initialId: string;
  isLoading?: boolean;
  hasNextPage?: boolean;
  hasPreviousPage?: boolean;
  isFetchingNextPage?: boolean;
  isFetchingPreviousPage?: boolean;
  onFetchNextPage?: () => void;
  onFetchPreviousPage?: () => void;
  onVisibleChange?: (recording: Recording) => void;
  onClose?: () => void;
  onNotFound?: () => void;
  showCloseButton?: boolean;
  height?: string;
}

export function VideoScrollPlayer({
  recordings,
  initialId,
  isLoading,
  hasNextPage,
  hasPreviousPage,
  isFetchingNextPage,
  isFetchingPreviousPage,
  onFetchNextPage,
  onFetchPreviousPage,
  onVisibleChange,
  onClose,
  onNotFound,
  showCloseButton = true,
  height = "100dvh",
}: VideoScrollPlayerProps) {
  const containerRef = useRef<HTMLDivElement>(null);
  const slideRefs = useRef<Map<number, HTMLDivElement>>(new Map());
  const [visibleIndex, setVisibleIndex] = useState<number | null>(null);
  const drawerListRef = useRef<HTMLDivElement>(null);
  const [drawerOpened, { open: openDrawer, close: closeDrawer }] =
    useDisclosure(false, {
      onOpen: () => {
        setTimeout(() => {
          drawerListRef.current
            ?.querySelector("[data-active]")
            ?.scrollIntoView({ block: "center", behavior: "instant" });
        }, 50);
      },
    });
  const isMobile = useMediaQuery("(max-width: 768px)");
  const hasScrolledToInitial = useRef(false);
  // Start muted for everyone — browsers only allow unattended autoplay when
  // muted + playsinline. User can unmute; the state persists across videos.
  const [isMuted, setIsMuted] = useState(true);
  const [hasPlayed, setHasPlayed] = useState(true);

  // Track the currently visible video's documentId (not index, since index can shift)
  const visibleDocumentId = useRef<string | null>(null);
  const prevRecordingsLength = useRef(recordings.length);

  // Find initial index
  const initialIndex = useMemo(
    () => recordings.findIndex((r) => r.documentId === initialId),
    [recordings, initialId],
  );

  // Handle not found case — only fire when there are no more pages to load
  const isNotFound =
    recordings.length > 0 &&
    initialIndex === -1 &&
    !hasNextPage &&
    !isFetchingNextPage;

  useEffect(() => {
    if (isNotFound && onNotFound) {
      onNotFound();
    }
  }, [isNotFound, onNotFound]);

  // When recordings change (new page loaded), maintain scroll position
  useLayoutEffect(() => {
    // If videos were prepended (previous page loaded)
    if (
      visibleDocumentId.current &&
      recordings.length > prevRecordingsLength.current
    ) {
      // Find where our current video is now
      const newIndex = recordings.findIndex(
        (r) => r.documentId === visibleDocumentId.current,
      );

      if (newIndex !== -1 && newIndex !== visibleIndex) {
        // Scroll to maintain position (instant, no animation)
        requestAnimationFrame(() => {
          const target = slideRefs.current.get(newIndex);
          target?.scrollIntoView({ behavior: "instant" });
        });
      }
    }

    prevRecordingsLength.current = recordings.length;
  }, [recordings, visibleIndex]);

  // Scroll to initial recording once when data loads
  useEffect(() => {
    if (hasScrolledToInitial.current) return;
    if (recordings.length === 0) return;
    if (initialIndex === -1) return;

    if (initialIndex === 0) {
      hasScrolledToInitial.current = true;
      visibleDocumentId.current = recordings[0]?.documentId || null;
      return;
    }

    requestAnimationFrame(() => {
      const target = slideRefs.current.get(initialIndex);
      target?.scrollIntoView({ behavior: "instant" });
    });

    hasScrolledToInitial.current = true;
    visibleDocumentId.current = recordings[initialIndex]?.documentId || null;
  }, [initialIndex, recordings]);

  const goToPrev = useCallback(() => {
    if (visibleIndex === null) return;

    if (visibleIndex > 0) {
      // There's a previous video in array, scroll to it
      const target = slideRefs.current.get(visibleIndex - 1);
      target?.scrollIntoView({ behavior: "smooth" });
    } else if (hasPreviousPage && !isFetchingPreviousPage) {
      // At start of array, fetch previous page
      // After it loads, useLayoutEffect will handle scroll position
      onFetchPreviousPage?.();
    }
  }, [
    visibleIndex,
    hasPreviousPage,
    isFetchingPreviousPage,
    onFetchPreviousPage,
  ]);

  const goToNext = useCallback(() => {
    if (visibleIndex === null) return;

    if (visibleIndex < recordings.length - 1) {
      const target = slideRefs.current.get(visibleIndex + 1);
      target?.scrollIntoView({ behavior: "smooth" });
    } else if (hasNextPage && !isFetchingNextPage) {
      // At end of array, fetch next page
      onFetchNextPage?.();
    }
  }, [
    visibleIndex,
    recordings.length,
    hasNextPage,
    isFetchingNextPage,
    onFetchNextPage,
  ]);

  const handleVisibleChange = useCallback(
    (index: number) => {
      setVisibleIndex(index);

      const recording = recordings[index];
      if (!recording) return;

      // Track by documentId so we can find it after array changes
      visibleDocumentId.current = recording.documentId!;

      onVisibleChange?.(recording);

      // Prefetch next page when near end
      const isNearEnd = index >= recordings.length - 3;
      if (isNearEnd && hasNextPage && !isFetchingNextPage) {
        onFetchNextPage?.();
      }

      // Prefetch previous page when near start
      const isNearStart = index <= 2;
      if (isNearStart && hasPreviousPage && !isFetchingPreviousPage) {
        onFetchPreviousPage?.();
      }
    },
    [
      recordings,
      hasNextPage,
      hasPreviousPage,
      isFetchingNextPage,
      isFetchingPreviousPage,
      onFetchNextPage,
      onFetchPreviousPage,
      onVisibleChange,
    ],
  );

  // Keyboard navigation
  useEffect(() => {
    const handleKeyDown = (e: KeyboardEvent) => {
      if (e.key === "ArrowUp") {
        e.preventDefault();
        goToPrev();
      } else if (e.key === "ArrowDown") {
        e.preventDefault();
        goToNext();
      } else if (e.key === "Escape" && onClose) {
        onClose();
      }
    };
    window.addEventListener("keydown", handleKeyDown);
    return () => window.removeEventListener("keydown", handleKeyDown);
  }, [goToPrev, goToNext, onClose]);

  if (isLoading) {
    return (
      <Flex h={height} justify="center" align="center">
        <Loader size="lg" />
      </Flex>
    );
  }

  if (isNotFound) {
    return (
      <Flex h={height} justify="center" align="center">
        <Stack align="center" gap="md">
          <IconVideoOff size={64} stroke={1.5} color="gray" />
          <Text size="lg" c="dimmed">
            Recording not found
          </Text>
          {onClose && (
            <ActionIcon variant="light" size="lg" onClick={onClose}>
              <IconX />
            </ActionIcon>
          )}
        </Stack>
      </Flex>
    );
  }

  const safeInitialIndex = initialIndex === -1 ? 0 : initialIndex;

  // Show prev if there's a video before OR if there's a previous page to load
  const showPrev =
    visibleIndex !== null && (visibleIndex > 0 || hasPreviousPage);
  const showNext =
    visibleIndex !== null &&
    (visibleIndex < recordings.length - 1 || hasNextPage);

  return (
    <Box pos="relative" h={height}>
      {/* Close button */}
      {showCloseButton && onClose && (
        <ActionIcon
          variant="filled"
          radius="xl"
          size="input-md"
          bg="rgba(255, 255, 255, 0.15)"
          color="gray"
          onClick={(e) => {
            e.stopPropagation();
            onClose();
          }}
          pos="absolute"
          top={20}
          left={20}
          style={{ zIndex: 100 }}
        >
          <IconX />
        </ActionIcon>
      )}

      {/* Navigation buttons — hidden when drawer is open */}
      {!drawerOpened && (
        <Group
          pos="absolute"
          right={20}
          top="50%"
          gap="xs"
          style={{
            transform: "translateY(-50%)",
            flexDirection: "column",
            zIndex: 100,
          }}
        >
          {visibleIndex !== null && recordings[visibleIndex]?.follower && (
            <Flex
              align="center"
              gap={4}
              direction="column"
              component={Link}
              href={getProfileUrl(recordings[visibleIndex].follower)}
              style={{ textDecoration: "none" }}
            >
              <Box pos="relative">
                <Avatar size={48} radius="xl">
                  {recordings[visibleIndex].follower?.avatar?.url && (
                    <Image
                      src={generateAvatarUrl(
                        recordings[visibleIndex].follower!.avatar!.url!,
                      )}
                      alt=""
                      width={48}
                      height={48}
                    />
                  )}
                </Avatar>
                <Box pos="absolute" bottom={-4} right={-4}>
                  <FollowerTypeIcon
                    type={recordings[visibleIndex].follower?.type}
                    size={18}
                    radius="xl"
                  />
                </Box>
              </Box>
              <Text size="xs" c="white" fw={500} maw={80} truncate ta="center">
                {decodeURIComponent(
                  recordings[visibleIndex].follower?.username || "",
                )}
              </Text>
            </Flex>
          )}
          {showPrev && (
            <ActionIcon
              variant="filled"
              radius="xl"
              size="input-md"
              bg="rgba(255, 255, 255, 0.15)"
              color="gray"
              onClick={goToPrev}
              loading={isFetchingPreviousPage}
            >
              <IconChevronUp />
            </ActionIcon>
          )}
          {showNext && (
            <ActionIcon
              variant="filled"
              radius="xl"
              size="input-md"
              bg="rgba(255, 255, 255, 0.15)"
              color="gray"
              onClick={goToNext}
              loading={isFetchingNextPage}
            >
              <IconChevronDown />
            </ActionIcon>
          )}
          <ActionIcon
            variant="filled"
            radius="xl"
            size="input-md"
            bg="rgba(255, 255, 255, 0.15)"
            color="gray"
            onClick={openDrawer}
          >
            <IconList />
          </ActionIcon>
        </Group>
      )}

      {/* Recording list drawer */}
      <Drawer
        opened={drawerOpened}
        onClose={closeDrawer}
        position="right"
        size={isMobile ? 150 : 300}
        withCloseButton
        title={null}
        styles={{
          body: { padding: 0 },
        }}
        withOverlay={false}
        style={{ zIndex: 200 }}
      >
        <ScrollArea h="calc(100dvh - 60px)" viewportRef={drawerListRef}>
          <Stack gap={0}>
            {recordings.map((rec, index) => {
              const isActive = index === visibleIndex;
              const firstDone = rec.sources?.find((s) => s.state === "done");
              const duration =
                rec.sources?.reduce(
                  (sum, s) => sum + (s.duration || 0),
                  0,
                ) || 0;

              return (
                <Stack
                  key={rec.documentId}
                  gap={4}
                  p="xs"
                  data-active={isActive || undefined}
                  onClick={() => {
                    const target = slideRefs.current.get(index);
                    target?.scrollIntoView({ behavior: "instant" });
                    if (isMobile) closeDrawer();
                  }}
                  style={{
                    cursor: "pointer",
                    background: isActive
                      ? "rgba(99, 102, 241, 0.15)"
                      : "transparent",
                    borderLeft: isActive
                      ? "3px solid var(--mantine-color-violet-6)"
                      : "3px solid transparent",
                  }}
                >
                  <Box
                    w="100%"
                    style={{
                      aspectRatio: "16/9",
                      borderRadius: 6,
                      overflow: "hidden",
                      position: "relative",
                      background: "#1a1b1e",
                    }}
                  >
                    {firstDone && (
                      <Image
                        src={getImageUrl(
                          rec.documentId!,
                          "preview.jpg",
                          firstDone,
                        )}
                        alt=""
                        fill
                        sizes="300px"
                        style={{ objectFit: "cover" }}
                        unoptimized
                      />
                    )}
                    {duration > 0 && (
                      <Text
                        size="xs"
                        fw={500}
                        c="white"
                        style={{
                          position: "absolute",
                          bottom: 4,
                          right: 6,
                          background: "rgba(0,0,0,0.7)",
                          padding: "1px 5px",
                          borderRadius: 3,
                          fontSize: 11,
                        }}
                      >
                        {formatDuration(duration)}
                      </Text>
                    )}
                    {rec.sources && rec.sources.length > 1 && (
                      <Text
                        size="xs"
                        fw={500}
                        c="white"
                        style={{
                          position: "absolute",
                          top: 4,
                          left: 6,
                          background: "rgba(0,0,0,0.7)",
                          padding: "1px 5px",
                          borderRadius: 3,
                          fontSize: 11,
                        }}
                      >
                        {rec.sources.length} parts
                      </Text>
                    )}
                    {rec.follower?.type && (
                      <Box pos="absolute" top={4} right={6}>
                        <FollowerTypeIcon
                          type={rec.follower.type}
                          color="black"
                          opacity={0.8}
                          size={18}
                        />
                      </Box>
                    )}
                  </Box>
                  <Text size="xs" c={isActive ? "white" : "dimmed"} fw={isActive ? 600 : 400}>
                    {rec.createdAt
                      ? new Date(rec.createdAt).toLocaleDateString(
                          undefined,
                          {
                            month: "short",
                            day: "numeric",
                            hour: "2-digit",
                            minute: "2-digit",
                          },
                        )
                      : ""}
                  </Text>
                </Stack>
              );
            })}
            {hasNextPage && (
              <Flex
                justify="center"
                p="md"
                ref={(el) => {
                  if (!el) return;
                  const observer = new IntersectionObserver(
                    ([entry]) => {
                      if (entry.isIntersecting && !isFetchingNextPage) {
                        onFetchNextPage?.();
                      }
                    },
                    { threshold: 0.1 },
                  );
                  observer.observe(el);
                  return () => observer.disconnect();
                }}
              >
                <Loader size="sm" />
              </Flex>
            )}
          </Stack>
        </ScrollArea>
      </Drawer>

      {/* Scroll container */}
      <Box
        ref={containerRef}
        h="100%"
        className="no-scrollbar"
        style={{
          overflowY: "auto",
          scrollSnapType: "y mandatory",
          scrollBehavior: "smooth",
          scrollbarWidth: "none",
          msOverflowStyle: "none",
          overscrollBehavior: "contain",
          position: "relative",
          zIndex: 1,
        }}
      >
        {recordings.map((recording, index) => (
          <VideoSlide
            key={recording.documentId}
            recording={recording}
            index={index}
            isInitiallyVisible={index === safeInitialIndex}
            onVisible={handleVisibleChange}
            isMuted={isMuted}
            onMuteChange={setIsMuted}
            autoPlay={hasPlayed}
            onPlay={() => setHasPlayed(true)}
            registerRef={(el) => {
              if (el) slideRefs.current.set(index, el);
              else slideRefs.current.delete(index);
            }}
          />
        ))}
      </Box>
    </Box>
  );
}

// VideoSlide component stays the same
function VideoSlide({
  recording,
  index,
  isInitiallyVisible,
  onVisible,
  isMuted,
  onMuteChange,
  autoPlay,
  onPlay,
  registerRef,
}: {
  recording: Recording;
  index: number;
  isInitiallyVisible: boolean;
  onVisible: (index: number) => void;
  isMuted: boolean;
  onMuteChange: (muted: boolean) => void;
  autoPlay: boolean;
  onPlay: () => void;
  registerRef: (el: HTMLDivElement | null) => void;
}) {
  const ref = useRef<HTMLDivElement>(null);
  const [isVisible, setIsVisible] = useState(isInitiallyVisible);
  const onVisibleRef = useRef(onVisible);

  useEffect(() => {
    onVisibleRef.current = onVisible;
  }, [onVisible]);

  useEffect(() => {
    registerRef(ref.current);
    return () => registerRef(null);
  }, [registerRef]);

  useEffect(() => {
    const element = ref.current;
    if (!element) return;

    const observer = new IntersectionObserver(
      ([entry]) => {
        setIsVisible(entry.isIntersecting);
        if (entry.isIntersecting) {
          onVisibleRef.current(index);
        }
      },
      { threshold: 0.5 },
    );

    observer.observe(element);
    return () => observer.disconnect();
  }, [index]);

  return (
    <Flex
      ref={ref}
      h="100%"
      justify="center"
      align="center"
      pos="relative"
      style={{
        scrollSnapAlign: "start",
        scrollSnapStop: "always",
      }}
    >
      {isVisible ? (
        <>
          <VideoPlayer
            recording={recording}
            key={recording.documentId}
            defaultMuted={isMuted}
            onMuteChange={onMuteChange}
            autoPlay={autoPlay}
            onPlay={onPlay}
          />
          <VideoLoadIndicator
            key={`indicator-${recording.documentId}`}
            containerRef={ref}
          />
        </>
      ) : (
        <Box w="100%" h="100%" bg="dark.9" />
      )}
    </Flex>
  );
}
