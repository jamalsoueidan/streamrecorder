"use client";

import { Recording } from "@/lib/types/strapi-autogenerated";
import { Modal, Text } from "@mantine/core";
import { useMediaQuery } from "@mantine/hooks";
import { useFormatter, useTranslations } from "next-intl";
import VideoEditor from "../protected/[type]/[username]/video/[id]/edit/video-editor";

interface VideoEditorModalProps {
  recording: Recording;
  opened: boolean;
  onClose: () => void;
}

export function VideoEditorModal({
  recording,
  opened,
  onClose,
}: VideoEditorModalProps) {
  const format = useFormatter();
  const isMobile = useMediaQuery("(max-width: 768px)");
  const t = useTranslations("protected.videoEditor");

  return (
    <Modal
      opened={opened}
      onClose={onClose}
      fullScreen={isMobile}
      size="90%"
      radius="md"
      styles={{
        content: {
          border: "1px solid var(--mantine-color-default-border)",
        },
      }}
      title={
        <Text fw="bold">
          {t("modal.title")}{" "}
          {format.dateTime(new Date(recording.createdAt || ""), {
            dateStyle: "medium",
            timeStyle: "short",
          })}
        </Text>
      }
    >
      <VideoEditor recording={recording} />
    </Modal>
  );
}
