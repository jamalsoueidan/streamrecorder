"use client";

import { useRole } from "@/app/providers/ability-provider";
import { useUser } from "@/app/providers/user-provider";
import { GetUsersPermissionsUsersRolesData } from "@/lib/types/strapi-autogenerated";
import {
  Alert,
  Anchor,
  Badge,
  Button,
  Card,
  Divider,
  Flex,
  Grid,
  GridCol,
  Group,
  Modal,
  Paper,
  Radio,
  Stack,
  Text,
  Title,
} from "@mantine/core";
import { useDisclosure } from "@mantine/hooks";
import {
  IconBell,
  IconCheck,
  IconCrown,
  IconDeviceAnalytics,
  IconDownload,
  IconEye,
  IconHeadset,
  IconLanguage,
  IconMovie,
  IconScissors,
  IconSparkles,
  IconUsers,
  IconVideo,
} from "@tabler/icons-react";
import Script from "next/script";
import { useEffect, useRef, useState } from "react";
import {
  AmexIcon,
  DiscoverIcon,
  MastercardIcon,
  PaypalIcon,
  VisaIcon,
} from "react-svg-credit-card-payment-icons";
import { activatePremium, cancelSubscription } from "./actions";

// Extend user type with subscription fields from schema
type UserWithSubscription = GetUsersPermissionsUsersRolesData & {
  subscriptionStatus?: "active" | "cancelled" | "expired";
  subscriptionEndDate?: string;
  billingPeriod?: string;
};

interface FSCheckoutHandler {
  open: (options: {
    sandbox?: unknown;
    name?: string;
    billing_cycle?: "monthly" | "annual" | "lifetime";
    licenses?: number;
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    purchaseCompleted?: (response: any) => void;
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    success?: (response: any) => void;
  }) => void;
}

declare global {
  interface Window {
    FS: {
      Checkout: new (options: {
        product_id: string;
        plan_id: string;
        public_key: string;
        image?: string;
      }) => FSCheckoutHandler;
    };
  }
}

const BILLING_OPTIONS = [
  {
    id: "1month",
    label: "1 Month",
    price: 10,
    perMonth: 10,
    savings: null,
    badge: null,
    billingCycle: "monthly" as const,
  },
  {
    id: "12months",
    label: "12 Months",
    price: 84,
    perMonth: 7,
    savings: "3 months free",
    badge: "Best Value",
    billingCycle: "annual" as const,
  },
  {
    id: "lifetime",
    label: "Lifetime",
    price: 300,
    perMonth: null,
    savings: "Pay once, use forever",
    badge: "Limited",
    billingCycle: "lifetime" as const,
  },
];

const CURRENT_PLAN_FEATURES = [
  { icon: IconUsers, label: "Record up to 3 of your own channels" },
  { icon: IconEye, label: "Watch your recordings" },
];

const PREMIUM_FEATURES = [
  {
    icon: IconUsers,
    label: "Record up to 100 of your own channels",
    color: "#a78bfa",
  },
  {
    icon: IconDownload,
    label: "Full control over your recordings",
    color: "#60a5fa",
  },
  { icon: IconBell, label: "Notifications when ready", color: "#fbbf24" },
  { icon: IconMovie, label: "AI highlights & clips", color: "#34d399" },
  { icon: IconLanguage, label: "AI subtitles & translation", color: "#f472b6" },
  { icon: IconSparkles, label: "AI memes & GIFs", color: "#fb923c" },
  { icon: IconScissors, label: "Publish to social media", color: "#38bdf8" },
  { icon: IconVideo, label: "Watch later & favorites", color: "#c084fc" },
  { icon: IconHeadset, label: "Priority support", color: "#a78bfa" },
];

const PAYMENT_PROVIDERS = [
  {
    id: "freemius",
    label: "Freemius",
    description: "Credit Card, PayPal, and more",
  },
];

export default function PricingPage() {
  const [selectedBilling, setSelectedBilling] = useState("12months");
  const [selectedPayment, setSelectedPayment] = useState<string | null>(
    "freemius",
  );
  const [fsReady, setFsReady] = useState(false);
  const handlerRef = useRef<FSCheckoutHandler | null>(null);

  const user = useUser() as UserWithSubscription | null;
  const role = useRole();
  const [
    cancelModalOpened,
    { open: openCancelModal, close: closeCancelModal },
  ] = useDisclosure(false);
  const [cancelling, setCancelling] = useState(false);

  const selectedPlan = BILLING_OPTIONS.find((o) => o.id === selectedBilling);

  // Determine if user is premium (admin, champion, or has active/cancelled subscription)
  const isPremiumRole = role?.type === "admin" || role?.type === "champion";
  const hasActiveSubscription =
    user?.subscriptionStatus === "active" ||
    user?.subscriptionStatus === "cancelled";
  const isPremium = isPremiumRole || hasActiveSubscription;
  const canCancel = user?.subscriptionStatus === "active" && !isPremiumRole;

  // Set to false in production to disable sandbox mode
  const isSandbox = process.env.NODE_ENV === "development";

  const handleCancelSubscription = async () => {
    setCancelling(true);
    const result = await cancelSubscription();
    setCancelling(false);

    if (result.success) {
      closeCancelModal();
      window.location.reload();
    } else {
      console.error("Failed to cancel:", result.error);
    }
  };

  useEffect(() => {
    if (fsReady && window.FS) {
      handlerRef.current = new window.FS.Checkout({
        product_id: "24577",
        plan_id: "40817",
        public_key: "pk_de5cf8f11ea2d0037fb0c50ca1818",
        image: "https://www.livestreamrecorder.com/icon_clean.png",
      });
    }
  }, [fsReady]);

  const handlePurchase = async () => {
    if (!handlerRef.current) return;

    try {
      // Only fetch sandbox params in development
      let sandbox = undefined;
      if (isSandbox) {
        sandbox = await fetch("/api/freemius/sandbox").then((res) =>
          res.json(),
        );
        console.log("🧪 SANDBOX MODE ENABLED");
      }

      const selectedPlan = BILLING_OPTIONS.find(
        (o) => o.id === selectedBilling,
      );

      handlerRef.current.open({
        // Sandbox only in development - remove/undefined in production
        ...(sandbox && { sandbox }),
        name: selectedPlan?.label || "Premium",
        billing_cycle: selectedPlan?.billingCycle,
        licenses: 1,
        purchaseCompleted: async (response: {
          user: { id: string };
          purchase: {
            subscription_id: string;
            billing_cycle: number;
            next_payment: string;
          };
          license: { expiration: string };
        }) => {
          console.log("Purchase completed:", response);

          // Map billing cycle number to string
          const billingPeriod =
            response.purchase.billing_cycle === 1
              ? "monthly"
              : response.purchase.billing_cycle === 12
                ? "annual"
                : "lifetime";

          // Immediately activate premium
          const result = await activatePremium({
            freemiusUserId: response.user.id,
            subscriptionId: response.purchase.subscription_id,
            billingPeriod,
            subscriptionEndDate:
              response.purchase.next_payment || response.license.expiration,
          });

          if (!result.success) {
            console.error("Failed to activate premium:", result.error);
          } else {
            // Refresh page to show premium status
            window.location.reload();
          }
        },
        success: () => {
          console.log("Checkout closed after successful purchase");
        },
      });
    } catch (error) {
      console.error("Failed to open checkout:", error);
    }
  };

  return (
    <Stack w="100%">
      <Script
        src="https://checkout.freemius.com/js/v1/"
        strategy="afterInteractive"
        onLoad={() => setFsReady(true)}
      />

      {/* Header */}
      <Stack gap={4}>
        <Group gap="xs">
          <IconCrown size={28} color="#fbbf24" />
          <Title order={2}>Premium</Title>
          {!isPremium && (
            <Badge size="lg" variant="filled" color="green">
              7 Days Free
            </Badge>
          )}
        </Group>
        <Text size="sm" c="dimmed">
          {isPremium
            ? "You have access to everything LiveStreamRecorder has to offer"
            : "Get instant access to everything LiveStreamRecorder has to offer"}
        </Text>
      </Stack>

      <Divider mx={{ base: "-xs", sm: "-md" }} />

      <Grid gutter="xl">
        {/* Left Column - Plan & Features */}
        <GridCol span={{ base: 12, md: isPremium ? 12 : 7 }}>
          <Stack gap="lg">
            {/* Current Plan */}
            {isPremium ? (
              <Card
                padding="lg"
                radius="md"
                withBorder
                style={{
                  background:
                    "linear-gradient(135deg, rgba(139, 92, 246, 0.15) 0%, rgba(124, 58, 237, 0.08) 100%)",
                  borderColor: "rgba(139, 92, 246, 0.35)",
                }}
              >
                <Stack gap="md">
                  <Group gap="xs">
                    <IconCrown size={20} color="#fbbf24" />
                    <Text fw={600} size="lg">
                      {isPremiumRole ? role?.name : "Premium"}
                    </Text>
                    {user?.subscriptionStatus === "cancelled" && (
                      <Badge size="sm" variant="light" color="orange">
                        Cancelled
                      </Badge>
                    )}
                  </Group>

                  <Alert color="violet" variant="light">
                    <Text size="sm">
                      {isPremiumRole ? (
                        "You have full premium access as an admin/champion."
                      ) : user?.subscriptionStatus === "cancelled" ? (
                        <>
                          Your{" "}
                          <Text span fw={700}>
                            {user?.billingPeriod}
                          </Text>{" "}
                          subscription has been cancelled. You still have access
                          until{" "}
                          <Text span fw={700}>
                            {new Date(
                              user.subscriptionEndDate!,
                            ).toLocaleDateString()}
                          </Text>
                          .
                        </>
                      ) : (
                        <>
                          You are on the{" "}
                          <Text span fw={700}>
                            {user?.billingPeriod}
                          </Text>{" "}
                          plan. Your subscription renews on{" "}
                          <Text span fw={700}>
                            {new Date(
                              user?.subscriptionEndDate || "",
                            ).toLocaleDateString()}
                          </Text>
                          .
                        </>
                      )}
                    </Text>
                  </Alert>

                  {canCancel && (
                    <Button
                      variant="subtle"
                      color="gray"
                      size="xs"
                      onClick={openCancelModal}
                    >
                      Cancel subscription
                    </Button>
                  )}
                </Stack>
              </Card>
            ) : (
              <Card
                padding="lg"
                radius="md"
                withBorder
                style={{
                  background:
                    "linear-gradient(135deg, rgba(100, 116, 139, 0.15) 0%, rgba(71, 85, 105, 0.08) 100%)",
                  borderColor: "rgba(100, 116, 139, 0.3)",
                }}
              >
                <Stack gap="md">
                  <Group justify="space-between">
                    <div>
                      <Group gap="xs">
                        <IconVideo size={20} color="#94a3b8" />
                        <Text fw={600} size="lg">
                          Your Current Plan
                        </Text>
                      </Group>
                    </div>
                    <div style={{ textAlign: "right" }}>
                      <Text size="md" c="dimmed">
                        Free
                      </Text>
                    </div>
                  </Group>

                  <Divider
                    style={{ borderColor: "rgba(100, 116, 139, 0.3)" }}
                  />

                  <Grid gutter="xs">
                    {CURRENT_PLAN_FEATURES.map((feature) => (
                      <GridCol span={6} key={feature.label}>
                        <Group gap="xs" wrap="nowrap">
                          <feature.icon
                            size={16}
                            color="var(--mantine-color-green-6)"
                          />
                          <Text size="sm">{feature.label}</Text>
                        </Group>
                      </GridCol>
                    ))}
                  </Grid>

                  <Text size="xs" c="dimmed">
                    LiveStreamRecorder is a one-person project. Your support
                    helps keep the servers running and new features coming.
                    Thank you!
                  </Text>
                </Stack>
              </Card>
            )}

            {/* Billing Options - Only show for non-premium users */}
            {!isPremium && (
              <Card
                padding="lg"
                radius="md"
                withBorder
                style={{
                  background:
                    "linear-gradient(135deg, rgba(59, 130, 246, 0.12) 0%, rgba(37, 99, 235, 0.06) 100%)",
                  borderColor: "rgba(59, 130, 246, 0.3)",
                }}
              >
                <Stack gap="md">
                  <Group gap="xs">
                    <IconDeviceAnalytics size={20} color="#60a5fa" />
                    <Text fw={600} size="lg">
                      Choose Your Plan
                    </Text>
                  </Group>

                  <Radio.Group
                    value={selectedBilling}
                    onChange={setSelectedBilling}
                  >
                    <Stack gap="sm">
                      {BILLING_OPTIONS.map((option) => (
                        <Paper
                          key={option.id}
                          p="md"
                          radius="md"
                          withBorder
                          style={{
                            cursor: "pointer",
                            borderColor:
                              selectedBilling === option.id
                                ? "var(--mantine-color-violet-6)"
                                : undefined,
                            background:
                              selectedBilling === option.id
                                ? "rgba(139, 92, 246, 0.08)"
                                : undefined,
                          }}
                          onClick={() => setSelectedBilling(option.id)}
                        >
                          <Group justify="space-between" wrap="nowrap">
                            <Group gap="md" wrap="nowrap">
                              <Radio value={option.id} />
                              <div>
                                <Group gap="xs">
                                  <Text fw={500}>{option.label}</Text>
                                  {option.badge && (
                                    <Badge
                                      size="xs"
                                      variant="filled"
                                      color="violet"
                                    >
                                      {option.badge}
                                    </Badge>
                                  )}
                                </Group>
                                {option.savings && (
                                  <Text size="xs" c="green">
                                    {option.savings}
                                  </Text>
                                )}
                              </div>
                            </Group>
                            <div style={{ textAlign: "right" }}>
                              <Text fw={700} size="lg">
                                ${option.price}
                              </Text>
                              {option.perMonth ? (
                                <Text size="xs" c="dimmed">
                                  ${option.perMonth.toFixed(2)}/mo
                                </Text>
                              ) : (
                                <Text size="xs" c="dimmed">
                                  one-time
                                </Text>
                              )}
                            </div>
                          </Group>
                        </Paper>
                      ))}
                    </Stack>
                  </Radio.Group>
                </Stack>
              </Card>
            )}

            {/* Premium Features */}
            <Card
              padding="lg"
              radius="md"
              withBorder
              style={{
                background:
                  "linear-gradient(135deg, rgba(139, 92, 246, 0.15) 0%, rgba(124, 58, 237, 0.08) 100%)",
                borderColor: "rgba(139, 92, 246, 0.35)",
              }}
            >
              <Stack gap="md">
                <Group gap="xs">
                  <IconSparkles size={20} color="#a78bfa" />
                  <Text fw={600} size="lg">
                    {isPremium
                      ? "Your Premium Features"
                      : "Unlock Premium Features"}
                  </Text>
                </Group>

                <Grid gutter="sm">
                  {PREMIUM_FEATURES.map((feature) => (
                    <GridCol span={6} key={feature.label}>
                      <Group gap="xs" wrap="nowrap">
                        <feature.icon size={18} color={feature.color} />
                        <Text size="sm">{feature.label}</Text>
                      </Group>
                    </GridCol>
                  ))}
                </Grid>

                <Text size="xs" c="dimmed">
                  Premium includes all future features as they&apos;re released.{" "}
                  <Anchor href="/changelog" size="xs">
                    See what&apos;s new
                  </Anchor>
                </Text>
              </Stack>
            </Card>
          </Stack>
        </GridCol>

        {/* Right Column - Payment (only for non-premium users) */}
        {!isPremium && (
          <GridCol span={{ base: 12, md: 5 }}>
            <Card
              padding="lg"
              radius="md"
              withBorder
              style={{
                position: "sticky",
                top: 20,
                background:
                  "linear-gradient(135deg, rgba(34, 197, 94, 0.1) 0%, rgba(22, 163, 74, 0.05) 100%)",
                borderColor: "rgba(34, 197, 94, 0.3)",
              }}
            >
              <Stack gap="lg">
                <Group gap="xs">
                  <IconCrown size={20} color="#fbbf24" />
                  <Text fw={600} size="lg">
                    Payment Method
                  </Text>
                </Group>

                <Radio.Group
                  value={selectedPayment}
                  onChange={setSelectedPayment}
                >
                  <Stack gap="sm">
                    {PAYMENT_PROVIDERS.map((provider) => (
                      <Paper
                        key={provider.id}
                        p="md"
                        radius="md"
                        withBorder
                        style={{
                          cursor: "pointer",
                          borderColor:
                            selectedPayment === provider.id
                              ? "var(--mantine-color-green-6)"
                              : "rgba(255,255,255,0.1)",
                          background:
                            selectedPayment === provider.id
                              ? "rgba(34, 197, 94, 0.1)"
                              : "rgba(0,0,0,0.2)",
                        }}
                        onClick={() => setSelectedPayment(provider.id)}
                      >
                        <Stack gap="sm">
                          <Group justify="space-between" wrap="nowrap">
                            <Group gap="md" wrap="nowrap">
                              <Radio value={provider.id} color="green" />
                              <div>
                                <Text fw={500}>{provider.label}</Text>
                                <Text size="xs" c="dimmed">
                                  {provider.description}
                                </Text>
                              </div>
                            </Group>
                          </Group>

                          <Group gap="xs" mt="xs">
                            <VisaIcon width={40} />
                            <MastercardIcon width={40} />
                            <AmexIcon width={40} />
                            <DiscoverIcon width={40} />
                            <PaypalIcon width={40} />
                          </Group>
                        </Stack>
                      </Paper>
                    ))}
                  </Stack>
                </Radio.Group>

                <Divider />

                {/* Order Summary */}
                <Stack gap="xs">
                  <Group justify="space-between">
                    <Text size="sm" c="dimmed">
                      Plan
                    </Text>
                    <Text size="sm">{selectedPlan?.label}</Text>
                  </Group>
                  <Group justify="space-between">
                    <Text size="sm" c="dimmed">
                      Subtotal
                    </Text>
                    <Text size="sm">${selectedPlan?.price}</Text>
                  </Group>
                  {selectedPlan?.savings && (
                    <Group justify="space-between">
                      <Text size="sm" c="dimmed">
                        Discount
                      </Text>
                      <Text size="sm" c="green">
                        {selectedPlan.savings}
                      </Text>
                    </Group>
                  )}
                  <Divider my="xs" />
                  <Group justify="space-between">
                    <Text fw={600}>Total</Text>
                    <Text fw={700} size="xl" c="violet">
                      ${selectedPlan?.price}
                    </Text>
                  </Group>
                </Stack>

                {/* VAT Notice */}
                <Text size="xs" c="dimmed" ta="center">
                  * The applicable Value Added Tax (VAT) will be calculated and
                  added to your total during the final payment process.
                </Text>

                {/* Pay Button */}
                <Button
                  fullWidth
                  size="lg"
                  radius="md"
                  onClick={handlePurchase}
                  disabled={!selectedPayment}
                  style={{
                    background:
                      "linear-gradient(135deg, #8b5cf6 0%, #6366f1 100%)",
                  }}
                >
                  Start 7-Day Free Trial
                </Button>

                {/* Security Note */}
                <Flex gap="xs" align="center" justify="center">
                  <IconCheck size={14} color="var(--mantine-color-green-6)" />
                  <Text size="xs" c="dimmed">
                    Secure payment powered by Freemius
                  </Text>
                </Flex>
              </Stack>
            </Card>
          </GridCol>
        )}
      </Grid>

      {/* Cancel Subscription Modal */}
      <Modal
        opened={cancelModalOpened}
        onClose={closeCancelModal}
        title="Cancel Subscription"
        centered
      >
        <Stack gap="md">
          <Text size="sm">
            We&apos;re sorry to see you go! If you cancel, you&apos;ll still
            have access to premium features until{" "}
            <Text span fw={700}>
              {user?.subscriptionEndDate
                ? new Date(user.subscriptionEndDate).toLocaleDateString()
                : "the end of your billing period"}
            </Text>
            .
          </Text>
          <Text size="sm" c="dimmed">
            After that, your account will return to the free plan.
          </Text>
          <Group justify="flex-end" mt="md">
            <Button variant="default" onClick={closeCancelModal}>
              Keep subscription
            </Button>
            <Button
              color="red"
              onClick={handleCancelSubscription}
              loading={cancelling}
            >
              Yes, cancel
            </Button>
          </Group>
        </Stack>
      </Modal>
    </Stack>
  );
}
