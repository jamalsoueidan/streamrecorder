"use client";

import { VideoPlayer } from "@/app/[locale]/my/components/video/video-player";
import { useWatchLater } from "@/app/hooks/use-watch-later";
import { Link, useRouter } from "@/i18n/navigation";
import { Recording } from "@/lib/types/strapi-autogenerated";
import {
  AspectRatio,
  Box,
  Button,
  Flex,
  Group,
  Paper,
  Stack,
  Switch,
  Text,
  ThemeIcon,
  Title,
} from "@mantine/core";
import { IconBookmark } from "@tabler/icons-react";
import { keepPreviousData, useQuery } from "@tanstack/react-query";
import { useTranslations } from "next-intl";
import { useSearchParams } from "next/navigation";
import { useCallback, useMemo, useState } from "react";
import { getRecordingsByIds } from "../actions";
import { WatchLaterQueue } from "./watch-later-queue";

interface Props {
  initialIds: string[];
  initialRecordings: Recording[];
}

export function WatchLaterTheater({
  initialIds,
  initialRecordings,
}: Props) {
  const t = useTranslations("protected.watchLater");
  const router = useRouter();
  const searchParams = useSearchParams();
  const { watchLater: clientWatchLater, isInitialized, removeFromWatchLater } =
    useWatchLater();

  const [autoplay, setAutoplay] = useState(true);

  // Use client state once initialized so cookie-side removals reflect instantly
  const ids = isInitialized ? clientWatchLater : initialIds;

  const { data: recordings = initialRecordings } = useQuery({
    queryKey: ["watch-later-theater", ids],
    queryFn: () => getRecordingsByIds(ids),
    enabled: ids.length > 0,
    initialData: initialRecordings,
    placeholderData: keepPreviousData,
  });

  // Order recordings by cookie order (cookie is the source of truth)
  const sortedRecordings = useMemo(
    () =>
      ids
        .map((id) => recordings.find((r: Recording) => r.documentId === id))
        .filter(Boolean) as Recording[],
    [ids, recordings],
  );

  // Resolve current video: ?v= query param, else first item
  const requestedId = searchParams.get("v");
  const currentRecording = useMemo(() => {
    if (requestedId) {
      const found = sortedRecordings.find((r) => r.documentId === requestedId);
      if (found) return found;
    }
    return sortedRecordings[0];
  }, [requestedId, sortedRecordings]);

  const currentIndex = currentRecording
    ? sortedRecordings.findIndex(
        (r) => r.documentId === currentRecording.documentId,
      )
    : -1;

  const selectVideo = useCallback(
    (documentId: string) => {
      router.replace(`/my/watch-later?v=${documentId}`, { scroll: false });
    },
    [router],
  );

  const handleEnded = useCallback(() => {
    if (!autoplay) return;
    const next = sortedRecordings[currentIndex + 1];
    if (next?.documentId) {
      selectVideo(next.documentId);
    }
  }, [autoplay, currentIndex, sortedRecordings, selectVideo]);

  const handleRemove = useCallback(
    (documentId: string) => {
      const wasCurrent = documentId === currentRecording?.documentId;
      const removedIdx = sortedRecordings.findIndex(
        (r) => r.documentId === documentId,
      );

      removeFromWatchLater(documentId);

      if (wasCurrent) {
        // After removal, the item that was at removedIdx+1 now sits at removedIdx
        const next = sortedRecordings[removedIdx + 1];
        if (next?.documentId) {
          selectVideo(next.documentId);
        } else {
          // No next — fall back to whatever the new first item is
          const newFirst = sortedRecordings.find(
            (r) => r.documentId !== documentId,
          );
          if (newFirst?.documentId) selectVideo(newFirst.documentId);
        }
      }
    },
    [currentRecording, removeFromWatchLater, selectVideo, sortedRecordings],
  );

  if (!sortedRecordings.length) {
    return (
      <Stack align="center" gap="md" py={80}>
        <ThemeIcon size={80} radius="xl" variant="light" color="gray">
          <IconBookmark size={40} stroke={1.5} />
        </ThemeIcon>
        <Stack gap={4} align="center">
          <Title order={3}>{t("emptyState.title")}</Title>
          <Text c="dimmed" size="sm" ta="center" maw={420}>
            {t("emptyState.description")}
          </Text>
        </Stack>
        <Button component={Link} href="/my/dashboard" variant="light" mt="sm">
          {t("emptyState.cta")}
        </Button>
      </Stack>
    );
  }

  return (
    <Flex
      direction={{ base: "column", md: "row" }}
      gap="md"
      align="flex-start"
    >
      <Box style={{ flex: 7, width: "100%", minWidth: 0 }}>
        {currentRecording && (
          <Paper
            withBorder
            radius="md"
            style={{ overflow: "hidden", background: "#000" }}
          >
            <AspectRatio ratio={16 / 9}>
              <VideoPlayer
                key={currentRecording.documentId}
                recording={currentRecording}
                defaultMuted={false}
                autoPlay
                inline
                onEnded={handleEnded}
              />
            </AspectRatio>
          </Paper>
        )}

        {currentRecording?.follower && (
          <Stack gap={2} mt="sm">
            <Title order={4}>
              {currentRecording.follower.nickname ??
                currentRecording.follower.username}
            </Title>
            <Text size="sm" c="dimmed">
              @{currentRecording.follower.username}
            </Text>
          </Stack>
        )}
      </Box>

      <Paper
        withBorder
        radius="md"
        p="sm"
        style={{
          flex: 3,
          width: "100%",
          minWidth: 0,
          maxHeight: "calc(100vh - 140px)",
          display: "flex",
          flexDirection: "column",
        }}
      >
        <Group justify="space-between" mb="xs" wrap="nowrap">
          <Stack gap={0}>
            <Title order={5}>{t("queue.title")}</Title>
            <Text size="xs" c="dimmed">
              {t("queue.count", {
                current: currentIndex >= 0 ? currentIndex + 1 : 1,
                total: sortedRecordings.length,
              })}
            </Text>
          </Stack>
          <Switch
            size="xs"
            checked={autoplay}
            onChange={(e) => setAutoplay(e.currentTarget.checked)}
            label={autoplay ? t("queue.autoplayOn") : t("queue.autoplayOff")}
          />
        </Group>

        <Box style={{ flex: 1, overflowY: "auto", minHeight: 0 }}>
          <WatchLaterQueue
            recordings={sortedRecordings}
            currentId={currentRecording?.documentId}
            onSelect={selectVideo}
            onRemove={handleRemove}
          />
        </Box>
      </Paper>
    </Flex>
  );
}
