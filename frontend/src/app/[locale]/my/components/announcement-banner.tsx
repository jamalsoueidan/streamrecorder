"use client";

import { Announcement } from "@/lib/types/strapi-autogenerated";
import { CloseButton, Flex } from "@mantine/core";
import { useLocalStorage, useMounted } from "@mantine/hooks";
import { IconSpeakerphone } from "@tabler/icons-react";
import ReactMarkdown from "react-markdown";
import Link from "@/app/components/link";

interface AnnouncementBannerProps {
  announcement: Announcement | null;
}

export function AnnouncementBanner({ announcement }: AnnouncementBannerProps) {
  const mounted = useMounted();
  const [dismissedId, setDismissedId] = useLocalStorage({
    key: "dismissed-announcement",
    defaultValue: "",
  });

  if (!mounted || !announcement || dismissedId === announcement.documentId) return null;

  const colors: Record<string, string> = {
    info: "linear-gradient(150deg, #3b82f6, #2563eb)",
    warning: "linear-gradient(150deg, #f59e0b, #f97316)",
    important: "linear-gradient(150deg, #dc2626, #ef4444)",
  };

  const bg = colors[(announcement as any).type] || colors.info;

  return (
    <Flex
      align="center"
      justify="center"
      gap="xs"
      px="md"
      py={8}
      mb="sm"
      style={{
        background: bg,
        borderRadius: 8,
      }}
    >
      <IconSpeakerphone size={18} color="white" style={{ flexShrink: 0 }} />
      <div
        style={{
          flex: 1,
          color: "white",
          fontWeight: 600,
          fontSize: "0.875rem",
          lineHeight: 1.4,
        }}
      >
        <ReactMarkdown
          components={{
            // Strip wrapping <p> so the markdown lays out inline with the
            // icon + close button instead of breaking onto its own block.
            p: ({ children }) => <>{children}</>,
            a: ({ href, children }) => {
              const url = href ?? "#";
              const isExternal = /^https?:\/\//.test(url);
              if (isExternal) {
                return (
                  <a
                    href={url}
                    target="_blank"
                    rel="noopener noreferrer"
                    style={{
                      color: "white",
                      textDecoration: "underline",
                      fontWeight: 700,
                    }}
                  >
                    {children}
                  </a>
                );
              }
              return (
                <Link
                  href={url}
                  style={{
                    color: "white",
                    textDecoration: "underline",
                    fontWeight: 700,
                  }}
                >
                  {children}
                </Link>
              );
            },
          }}
        >
          {announcement.message}
        </ReactMarkdown>
      </div>
      <CloseButton
        c="white"
        size="sm"
        onClick={() => setDismissedId(announcement.documentId!)}
        style={{ flexShrink: 0 }}
      />
    </Flex>
  );
}
