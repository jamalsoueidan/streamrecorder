import api from "@/lib/api";
import { FollowerTypeEnum } from "@/lib/types/strapi-autogenerated";

export type FollowerEnrichment = {
  followerNickname?: string;
  followerAvatarUrl?: string;
};

export async function enrichWithFollowers<T extends { owner_username?: string }>(
  items: T[],
): Promise<(T & FollowerEnrichment)[]> {
  const usernames = items
    .map((i) => i.owner_username)
    .filter((u): u is string => !!u);

  const map: Record<string, FollowerEnrichment> = {};

  if (usernames.length > 0) {
    try {
      // POST /followers/search — body avoids URL truncation when the
      // insights surface 100+ usernames in the $in array.
      const res = await api.follower.searchFollowers({
        filters: {
          username: { $in: usernames },
          type: { $eq: FollowerTypeEnum.Tiktok },
        },
        populate: ["avatar"],
        pagination: { limit: usernames.length } as never,
      });
      for (const f of res.data?.data ?? []) {
        if (f.username) {
          map[f.username] = {
            followerNickname: f.nickname,
            followerAvatarUrl: f.avatar?.url,
          };
        }
      }
    } catch {
      // enrichment is optional — don't fail the whole request
    }
  }

  return items.map((item) => ({
    ...item,
    ...(item.owner_username ? (map[item.owner_username] ?? {}) : {}),
  }));
}
