"use server";

import api from "@/lib/api";
import publicApi from "@/lib/public-api";
import { ScopeEnum } from "@/lib/types/strapi-autogenerated";
import { deepMerge } from "@mantine/core";

const defaultOptions = {
  sort: "updatedAt:desc",
  filters: {
    sources: {
      state: {
        $eq: "recording",
      },
    },
  },
  populate: {
    sources: {
      filters: {
        state: {
          $eq: "recording",
        },
      },
    },
    follower: {
      fields: ["username", "type"],
      populate: {
        avatar: {
          fields: ["url"],
        },
      },
    },
  },
};

export async function fetchLiveRecordings(
  scope: string = ScopeEnum.Following,
  page: number = 1,
  type?: string,
) {
  // For "following" scope, get user's follower IDs and filter
  if (scope === ScopeEnum.Following) {
    const user =
      await api.usersPermissionsUsersRoles.getUsersPermissionsUsersRoles({
        populate: {
          followers: { fields: ["id"] },
        },
      });

    const followingIds =
      user.data?.followers?.map((f: any) => f.id) || [];

    // POST /recordings/search — body avoids URL truncation for 100+ followers.
    const response = await publicApi.recording.searchRecordings(
      deepMerge(defaultOptions, {
        pagination: { page, pageSize: 10, withCount: true },
        filters: {
          follower: {
            id: { $in: followingIds.length === 0 ? [0] : followingIds },
            ...(type ? { type: { $eq: type } } : {}),
          },
        },
      }),
    );

    return {
      data: response.data?.data || [],
      meta: response.data?.meta,
    };
  }

  // "discover" or "all" — no follower filter
  const response = await publicApi.recording.searchRecordings(
    deepMerge(defaultOptions, {
      pagination: { page, pageSize: 10, withCount: true },
      filters: {
        follower: type ? { type: { $eq: type } } : {},
      },
    }),
  );

  return {
    data: response.data?.data || [],
    meta: response.data?.meta,
  };
}
