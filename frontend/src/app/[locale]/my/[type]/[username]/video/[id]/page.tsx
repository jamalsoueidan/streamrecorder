"use client";

import { Box, Flex, Loader } from "@mantine/core";
import { useInfiniteQuery } from "@tanstack/react-query";
import { useParams, useRouter, useSearchParams } from "next/navigation";
import { useQueryStates } from "nuqs";
import { useCallback, useEffect, useMemo } from "react";

import { VideoScrollPlayer } from "@/app/[locale]/my/components/video/video-scroll-player";
import { getMyProfileUrl } from "@/app/lib/profile-url";
import { Recording } from "@/lib/types/strapi-autogenerated";
import { fetchProfileRecordings } from "../../actions/actions";
import { profileParsers } from "../../lib/search-params";

const SHELL_HEIGHT =
  "calc(100dvh - var(--app-shell-header-height, 0px) - var(--app-shell-footer-height, 0px) - var(--app-shell-padding) * 2)";

export default function VideoPage() {
  const router = useRouter();
  const params = useParams<{
    id: string;
    username: string;
    type: string;
  }>();
  const searchParams = useSearchParams();
  const [filters] = useQueryStates(profileParsers);

  const {
    data,
    fetchNextPage,
    hasNextPage,
    isFetchingNextPage,
    isLoading,
    isFetching,
  } = useInfiniteQuery({
    queryKey: ["profile-recordings", params.type, params.username, filters],
    queryFn: ({ pageParam }) =>
      fetchProfileRecordings(
        params.type,
        params.username,
        filters,
        pageParam,
      ),
    initialPageParam: 1,
    getNextPageParam: (lastPage) => {
      const { page = 1, pageSize } = lastPage.meta?.pagination ?? {};
      return lastPage.data.length === pageSize ? page + 1 : undefined;
    },
  });

  const recordings = useMemo(
    () => data?.pages.flatMap((p) => p.data) ?? [],
    [data],
  );

  const videoExists = recordings.some((r) => r.documentId === params.id);

  // Auto-fetch pages until target video appears (mirrors modal behavior).
  // Shares the same `profile-recordings` cache as parent profile page and
  // the @modal route — any pages we load here benefit them too.
  useEffect(() => {
    if (!videoExists && hasNextPage && !isFetchingNextPage) {
      fetchNextPage();
    }
  }, [videoExists, hasNextPage, isFetchingNextPage, fetchNextPage]);

  const handleVisibleChange = useCallback(
    (recording: Recording) => {
      const basePath = `${getMyProfileUrl(recording.follower)}/video/${recording.documentId}`;

      const search = searchParams.toString();
      const url = search ? `${basePath}?${search}` : basePath;
      window.history.replaceState(null, "", url);
    },
    [searchParams],
  );

  const handleNotFound = () => {
    router.replace(`/my/${params.type}/${decodeURIComponent(params.username)}`);
  };

  const handleClose = () => {
    // Go back to profile page
    router.push(`/my/${params.type}/${decodeURIComponent(params.username)}`);
  };

  if (isLoading || (isFetching && !videoExists)) {
    return (
      <Box h={SHELL_HEIGHT} pos="relative">
        <Flex h="100%" justify="center" align="center">
          <Loader size="lg" />
        </Flex>
      </Box>
    );
  }

  return (
    <Box
      h={SHELL_HEIGHT}
      pos="relative"
      style={{ borderRadius: "var(--mantine-radius-md)", overflow: "hidden" }}
    >
      <VideoScrollPlayer
        recordings={recordings}
        initialId={params.id}
        hasNextPage={hasNextPage}
        isFetchingNextPage={isFetchingNextPage}
        onFetchNextPage={fetchNextPage}
        onVisibleChange={handleVisibleChange}
        onClose={handleClose}
        onNotFound={handleNotFound}
        showCloseButton={true}
        height="100%"
      />
    </Box>
  );
}
