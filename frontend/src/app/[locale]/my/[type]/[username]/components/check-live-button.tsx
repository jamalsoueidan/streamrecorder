"use client";

import { Follower, FollowerTypeEnum } from "@/lib/types/strapi-autogenerated";
import { ActionIcon, Button } from "@mantine/core";
import { notifications } from "@mantine/notifications";
import { IconBroadcast } from "@tabler/icons-react";
import { useTranslations } from "next-intl";
import { useTransition } from "react";

import { triggerLiveStatusCheck } from "../actions/check-live";

export function CheckLiveButton({
  follower,
  showLabel,
}: {
  follower: Follower;
  showLabel?: boolean;
}) {
  const t = useTranslations("protected.profile.checkLive");
  const [isPending, startTransition] = useTransition();

  const handleClick = () => {
    startTransition(async () => {
      const result = await triggerLiveStatusCheck(
        follower.username!,
        follower.type as FollowerTypeEnum,
      );

      if (!result.ok) {
        notifications.show({
          color: "red",
          title: t("errorTitle"),
          message: result.error,
        });
        return;
      }

      if (result.isLive) {
        notifications.show({
          color: "green",
          title: t("liveTitle"),
          message: t("liveMessage"),
        });
      } else {
        notifications.show({
          color: "yellow",
          title: t("offlineTitle"),
          message: t("offlineMessage"),
        });
      }
    });
  };

  if (showLabel) {
    return (
      <Button
        size="sm"
        radius="xl"
        variant="light"
        color="red"
        loading={isPending}
        leftSection={<IconBroadcast size={18} />}
        onClick={handleClick}
      >
        {t("button")}
      </Button>
    );
  }

  return (
    <ActionIcon
      size="lg"
      variant="light"
      color="red"
      loading={isPending}
      onClick={handleClick}
      aria-label={t("button")}
    >
      <IconBroadcast />
    </ActionIcon>
  );
}
