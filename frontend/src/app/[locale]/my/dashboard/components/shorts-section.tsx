import Link from "@/app/components/link";
import { Clip } from "@/lib/types/strapi-autogenerated";
import {
  Box,
  Button,
  Divider,
  Flex,
  SimpleGrid,
  Title,
} from "@mantine/core";
import { IconChevronRight, IconScissors } from "@tabler/icons-react";
import { getTranslations } from "next-intl/server";
import { ShortCard } from "./short-card";

interface ShortsSectionProps {
  shortsPromise: Promise<Clip[]>;
}

export async function ShortsSection({ shortsPromise }: ShortsSectionProps) {
  const t = await getTranslations("protected.dashboard");
  const clips = await shortsPromise;

  if (!clips.length) return null;

  return (
    <>
      <Divider />
      <Box
        id="dashboard-shorts"
        style={{
          background: "linear-gradient(150deg, #FFB4E5, #FF52A8)",
        }}
        py="sm"
        px={{ base: "xs", sm: "md" }}
      >
        <Flex justify="space-between" align="center" w="100%">
          <Flex gap="xs" align="center">
            <IconScissors size={24} color="black" />
            <Title order={1} size="lg" c="black">
              {t("shorts")}
            </Title>
          </Flex>
          <Link href="/my/clips">
            <Button
              variant="transparent"
              color="black"
              size="compact-sm"
              rightSection={<IconChevronRight size={16} />}
            >
              {t("browseAll")}
            </Button>
          </Link>
        </Flex>
      </Box>
      <Divider />

      <Box p="md">
        <SimpleGrid cols={{ base: 2, sm: 3, md: 4, lg: 6 }} spacing="sm">
          {clips.map((clip) => (
            <ShortCard key={clip.documentId} clip={clip} />
          ))}
        </SimpleGrid>
      </Box>
    </>
  );
}
