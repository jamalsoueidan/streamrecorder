"use server";

import {
  fetchCachedRecordings,
  RecordingScope,
} from "@/app/actions/recordings";
import api from "@/lib/api";
import publicApi from "@/lib/public-api";
import { FollowerTypeEnum, ScopeEnum } from "@/lib/types/strapi-autogenerated";

// Distinct platform types the current user actually follows. Lets the MyFeed
// section query only those platforms instead of all ~18 — a user who follows
// creators on 2 platforms then fires 2 queries, not 18. Returns [] for users
// who follow nobody (MyFeed then renders nothing, zero queries).
export async function fetchFollowedPlatforms(): Promise<FollowerTypeEnum[]> {
  const response =
    await api.usersPermissionsUsersRoles.getUsersPermissionsUsersRoles({
      populate: { followers: { fields: ["type"] } },
    });
  const types = (response.data?.followers || [])
    .map((f: any) => f.type as FollowerTypeEnum)
    .filter(Boolean);
  return Array.from(new Set(types));
}

export async function fetchRecordingsByPlatform(
  scope: ScopeEnum,
  platform: FollowerTypeEnum,
) {
  const result = await fetchCachedRecordings({
    scope: scope as unknown as RecordingScope,
    filters: {
      follower: {
        type: { $eq: platform },
      },
    },
    sort: "createdAt:desc",
    page: 1,
    pageSize: 4,
  });
  return result.data || [];
}

// Dashboard "Discover" section — GLOBAL latest-per-platform feed.
// Uses scope "all" (no follower filter) instead of "discover" ($notIn the
// user's followingIds). That makes the result cached ONCE globally and shared
// across every user, instead of a unique per-user cache entry. This is what
// makes the dashboard survive restarts: the first request after a deploy warms
// one shared entry for everyone, rather than each user triggering 18 cold,
// expensive $notIn queries. Tradeoff: Discover is no longer personalized to
// exclude creators you already follow — acceptable for a discovery surface.
export async function fetchDiscoverByPlatform(platform: FollowerTypeEnum) {
  const result = await fetchCachedRecordings({
    scope: "all" as RecordingScope,
    filters: {
      follower: {
        type: { $eq: platform },
      },
    },
    sort: "createdAt:desc",
    page: 1,
    pageSize: 4,
  });
  return result.data || [];
}

