import { getUser } from "@/app/actions/user";
import { streamingPlatforms } from "@/app/lib/streaming-platforms";
import { FollowerTypeEnum, ScopeEnum } from "@/lib/types/strapi-autogenerated";
import {
  Box,
  Card,
  Divider,
  Flex,
  Skeleton,
  Stack,
  Title,
} from "@mantine/core";
import { IconWorldSearch } from "@tabler/icons-react";
import { getTranslations } from "next-intl/server";
import { Suspense } from "react";
import { fetchRandomClips } from "./actions/fetch-clips";
import {
  fetchDiscoverByPlatform,
  fetchFavoriteRecordings,
  fetchFollowedPlatforms,
  fetchLatestFollowers,
  fetchMostDownloadedRecordings,
  fetchMostViewedRecordings,
  fetchRecordingsByPlatform,
} from "./actions/fetch-recordings";
import { DashboardNav } from "./components/dashboard-nav";
import { DiscoverSection } from "./components/discover-section";
import { FavoritesSection } from "./components/favorites-section";
import { GreetingSection } from "./components/greeting-section";
import { LatestFollowersSection } from "./components/latest-followers-section";
import { MostDownloadedSection } from "./components/most-downloaded-section";
import { MostViewedSection } from "./components/most-viewed-section";
import { MyFeedSection } from "./components/my-feed-section";
import { PlatformsSection } from "./components/platforms-section";
import { ShortsSection } from "./components/shorts-section";

const platformMap: Record<string, FollowerTypeEnum> = {
  TikTok: FollowerTypeEnum.Tiktok,
  Twitch: FollowerTypeEnum.Twitch,
  Kick: FollowerTypeEnum.Kick,
  YouTube: FollowerTypeEnum.Youtube,
  AfreecaTV: FollowerTypeEnum.Afreecatv,
  Pandalive: FollowerTypeEnum.Pandalive,
  Bigo: FollowerTypeEnum.Bigo,
  LiveMe: FollowerTypeEnum.Liveme,
  Mixch: FollowerTypeEnum.Mixch,
  Twitcast: FollowerTypeEnum.Twitcast,
  Joilive: FollowerTypeEnum.Joilive,
  Live17: FollowerTypeEnum.Live17,
  Kwai: FollowerTypeEnum.Kwai,
  Nimotv: FollowerTypeEnum.Nimotv,
  Vklive: FollowerTypeEnum.Vklive,
  Chzzk: FollowerTypeEnum.Chzzk,
  YouNow: FollowerTypeEnum.Younow,
};

export default async function Page() {
  const t = await getTranslations("protected.dashboard");

  // Start all promises without awaiting - they run in parallel
  const userPromise = getUser();
  const followersPromise = fetchLatestFollowers();
  const favoritesPromise = fetchFavoriteRecordings();
  const mostViewedPromise = fetchMostViewedRecordings();
  const mostDownloadedPromise = fetchMostDownloadedRecordings();
  const shortsPromise = fetchRandomClips();

  // Only query platforms the user actually follows — not all ~18. Follows
  // nobody → zero MyFeed queries. Follows 2 platforms → 2 queries.
  const myFeedPromise = (async () => {
    const followedTypes = new Set(await fetchFollowedPlatforms());
    const followedPlatforms = streamingPlatforms.filter((platform) =>
      followedTypes.has(platformMap[platform.name]),
    );
    return Promise.all(
      followedPlatforms.map(async (platform) => {
        const type = platformMap[platform.name];
        const recordings = await fetchRecordingsByPlatform(
          ScopeEnum.Following,
          type,
        );
        return { ...platform, type, recordings };
      }),
    );
  })();

  const discoverPromise = Promise.all(
    streamingPlatforms.map(async (platform) => {
      const type = platformMap[platform.name];
      // Global, shared-across-users cache (see fetchDiscoverByPlatform) so a
      // restart warms one entry for everyone instead of N×18 per-user queries.
      const recordings = await fetchDiscoverByPlatform(type);
      return { ...platform, type, recordings };
    }),
  );

  return (
    <Stack w="100%">
      <Card radius="lg" withBorder p="0">
        <DashboardNav />

        <Suspense fallback={<Skeleton h={80} m="xl" radius="md" />}>
          <GreetingSection userPromise={userPromise} />
        </Suspense>

        <Divider />

        <PlatformsSection />

        <Divider />

        <Box
          id="dashboard-new-creators"
          py="sm"
          px={{ base: "xs", sm: "md" }}
          style={{ background: "linear-gradient(150deg, #B4F4FF, #52DDE8)" }}
        >
          <Flex justify="space-between" align="center" w="100%">
            <Stack gap={2}>
              <Flex gap="xs" align="center">
                <IconWorldSearch size={24} color="black" />
                <Title order={1} size="lg" c="black">
                  {t("newCreators")}
                </Title>
              </Flex>
            </Stack>
          </Flex>
        </Box>
        <Divider mx={{ base: "-xs", sm: "-md" }} />

        <Suspense fallback={<Skeleton h={120} radius="md" m="md" />}>
          <LatestFollowersSection followersPromise={followersPromise} />
        </Suspense>

        <Suspense fallback={<Skeleton h={340} radius="md" m="md" />}>
          <ShortsSection shortsPromise={shortsPromise} />
        </Suspense>

        <Suspense fallback={<Skeleton h={200} radius="md" m="md" />}>
          <FavoritesSection favoritesPromise={favoritesPromise} />
        </Suspense>

        <Suspense fallback={<Skeleton h={200} radius="md" m="md" />}>
          <MostViewedSection mostViewedPromise={mostViewedPromise} />
        </Suspense>

        <Suspense fallback={<Skeleton h={200} radius="md" m="md" />}>
          <MostDownloadedSection
            mostDownloadedPromise={mostDownloadedPromise}
          />
        </Suspense>

        <Suspense fallback={<Skeleton h={360} radius="md" m="md" />}>
          <MyFeedSection myFeedPromise={myFeedPromise} />
        </Suspense>

        <Suspense fallback={<Skeleton h={360} radius="md" m="md" />}>
          <DiscoverSection discoverPromise={discoverPromise} />
        </Suspense>
      </Card>
    </Stack>
  );
}
