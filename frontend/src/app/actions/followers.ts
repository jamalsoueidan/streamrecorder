"use server";

import api from "@/lib/api";
import publicApi from "@/lib/public-api";
import {
  FollowerTypeEnum,
  FollowRequestBody,
} from "@/lib/types/strapi-autogenerated";
import { revalidatePath } from "next/cache";

export async function follow(username: string, type: FollowerTypeEnum) {
  try {
    await api.follower.followCreate({
      username,
      type,
    });

    return { success: true, username, type };
  } catch (err: any) {
    return { error: err.response?.data?.error?.message || "Failed to follow" };
  }
}

export async function followUser(data: FollowRequestBody) {
  try {
    await api.follower.followCreate(data);
    revalidatePath("/my/following");
    revalidatePath(`/${data.type}/${data.username}`);
    revalidatePath(`/my/${data.type}/${data.username}`);

    return { success: true };
  } catch (err: any) {
    return { error: err.response?.data?.error?.message || "Failed to follow" };
  }
}

export async function unfollow(data: FollowRequestBody) {
  try {
    await api.follower.unfollowCreate(data);
    revalidatePath("/my/following");
    revalidatePath(`/${data.type}/${data.username}`);
    revalidatePath(`/my/${data.type}/${data.username}`);

    return { success: true };
  } catch (err: any) {
    return {
      error: err.response?.data?.error?.message || "Failed to unfollow",
    };
  }
}

export async function updateFollower(documentId: string, data: any) {
  await api.follower.putFollowersId({ id: documentId }, { data });
}

export async function blockFollowerAndPurge(followerDocumentId: string) {
  try {
    const userResponse =
      await api.usersPermissionsUsersRoles.getUsersPermissionsUsersRoles({
        populate: { role: { fields: ["type"] } },
      });
    const roleType = (userResponse?.data as any)?.role?.type;
    if (roleType !== "admin") {
      return { success: false, error: "FORBIDDEN" };
    }

    // 1. Flag the follower as blocked (across all locales)
    await publicApi.follower.putFollowersId(
      { id: followerDocumentId },
      { data: { blocked: true } as any },
    );

    // 2. Fetch all recordings for this follower with their source documentIds
    const { data: recResponse } = await publicApi.recording.getRecordings({
      filters: { follower: { documentId: { $eq: followerDocumentId } } } as any,
      populate: { sources: { fields: ["documentId"] } } as any,
      "pagination[pageSize]": 1000,
    });

    const recordings = (recResponse?.data || []) as Array<{
      documentId?: string;
      sources?: Array<{ documentId?: string }>;
    }>;

    const sourceIds = recordings
      .flatMap((r) => r.sources || [])
      .map((s) => s.documentId)
      .filter((id): id is string => !!id);

    const recordingIds = recordings
      .map((r) => r.documentId)
      .filter((id): id is string => !!id);

    // 3. Delete all sources first (so storage files get cleaned up),
    //    then delete the recording rows so nothing is left orphaned.
    await Promise.all(
      sourceIds.map((id) => publicApi.source.deleteSourcesId({ id })),
    );
    await Promise.all(
      recordingIds.map((id) =>
        publicApi.recording.deleteRecordingsId({ id }),
      ),
    );

    return {
      success: true,
      deletedSources: sourceIds.length,
      deletedRecordings: recordingIds.length,
    };
  } catch (error) {
    console.error("Error blocking follower:", error);
    return { success: false, error: "Failed to block follower" };
  }
}

export async function getFollowerFilters(type?: FollowerTypeEnum) {
  const { data: filtersData } = await publicApi.follower.getFollowerFilters(
    type ? { type } : { type: undefined },
  );

  return {
    genders: (filtersData.genders ?? [])
      .filter((g) => g.value)
      .map((g) => ({
        value: g.value || "",
        label: `${g.value} (${g.count})`,
      })),
    countryCodes: (filtersData.countryCodes ?? [])
      .filter((c) => c.value !== "-")
      .map((c) => ({
        value: c.value || "",
        label: `${c.value} (${c.count})`,
      })),
    types: (filtersData.types ?? []).map((l) => ({
      value: l.value || "",
      label: `${l.value} (${l.count})`,
    })),
    languageCodes: (filtersData.languageCodes ?? []).map((l) => ({
      value: l.value || "",
      label: `${l.value} (${l.count})`,
    })),
  };
}
