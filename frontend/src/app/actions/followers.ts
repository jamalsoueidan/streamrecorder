"use server";

import api from "@/lib/api";
import { purgeCloudflareUrls } from "@/lib/cloudflare";
import publicApi from "@/lib/public-api";
import {
  FollowerTypeEnum,
  FollowRequestBody,
} from "@/lib/types/strapi-autogenerated";
import { revalidatePath, revalidateTag } from "next/cache";
import { publicFollowerTag } from "@/app/[locale]/(public)/[type]/[username]/actions/actions";

const BASE_URL =
  process.env.NEXT_PUBLIC_BASE_URL || "https://www.livestreamrecorder.com";

export async function follow(username: string, type: FollowerTypeEnum) {
  try {
    await api.follower.followCreate({
      username,
      type,
    });

    return { success: true, username, type };
  } catch (err: any) {
    return { error: err.response?.data?.error?.message || "Failed to follow" };
  }
}

export async function followUser(data: FollowRequestBody) {
  try {
    await api.follower.followCreate(data);
    revalidatePath("/my/following");
    revalidatePath(`/${data.type}/${data.username}`);
    revalidatePath(`/my/${data.type}/${data.username}`);

    return { success: true };
  } catch (err: any) {
    return { error: err.response?.data?.error?.message || "Failed to follow" };
  }
}

export async function unfollow(data: FollowRequestBody) {
  try {
    await api.follower.unfollowCreate(data);
    revalidatePath("/my/following");
    revalidatePath(`/${data.type}/${data.username}`);
    revalidatePath(`/my/${data.type}/${data.username}`);

    return { success: true };
  } catch (err: any) {
    return {
      error: err.response?.data?.error?.message || "Failed to unfollow",
    };
  }
}

export async function updateFollower(documentId: string, data: any) {
  await api.follower.putFollowersId({ id: documentId }, { data });
}

export async function blockFollowerAndPurge(followerDocumentId: string) {
  try {
    const userResponse =
      await api.usersPermissionsUsersRoles.getUsersPermissionsUsersRoles({
        populate: { role: { fields: ["type"] } },
      });
    const roleType = (userResponse?.data as any)?.role?.type;
    if (roleType !== "admin") {
      return { success: false, error: "FORBIDDEN" };
    }

    // Fetch follower BEFORE blocking so we have the username + type to
    // build the URLs we need to invalidate downstream. After blockFollower
    // the row still exists (blocked=true), but pulling it first keeps the
    // call independent of any cache the backend might hold.
    const { data: followerData } = await publicApi.follower.getFollowersId({
      id: followerDocumentId,
      fields: ["username", "type"],
    } as never);
    const follower = (followerData as any)?.data as
      | { username?: string; type?: string }
      | undefined;

    const { data } = await publicApi.follower.blockFollower({
      documentId: followerDocumentId,
    });

    // Invalidate every public/protected surface that referenced this
    // follower so the deletion is visible immediately. We don't know the
    // individual deleted recording/clip URLs from the backend response
    // (only counts), but the creator-level pages cover the surfaces a
    // human would land on.
    if (follower?.type && follower?.username) {
      const { type, username } = follower;

      // Bust ONLY this follower's data cache — not all followers.
      // unstable_cache's tag is per-follower (`public-follower:bigo:dianad`
      // etc.), keyed off type+username at fetch time.
      revalidateTag(publicFollowerTag(type, username));

      revalidatePath(`/${type}/${username}`);
      revalidatePath(`/${type}/${username}/clips`);
      revalidatePath(`/my/${type}/${username}`);
      revalidatePath(`/my/${type}/${username}/clips`);

      await purgeCloudflareUrls([
        `${BASE_URL}/${type}/${username}`,
        `${BASE_URL}/${type}/${username}/clips`,
        `${BASE_URL}/my/${type}/${username}`,
        `${BASE_URL}/my/${type}/${username}/clips`,
      ]);
    }

    return {
      success: true,
      deletedSources: (data as any)?.deletedSources ?? 0,
      deletedRecordings: (data as any)?.deletedRecordings ?? 0,
      deletedClips: (data as any)?.deletedClips ?? 0,
    };
  } catch (error) {
    console.error("Error blocking follower:", error);
    return { success: false, error: "Failed to block follower" };
  }
}

export async function getFollowerFilters(type?: FollowerTypeEnum) {
  const { data: filtersData } = await publicApi.follower.getFollowerFilters(
    type ? { type } : { type: undefined },
  );

  return {
    genders: (filtersData.genders ?? [])
      .filter((g) => g.value)
      .map((g) => ({
        value: g.value || "",
        label: `${g.value} (${g.count})`,
      })),
    countryCodes: (filtersData.countryCodes ?? [])
      .filter((c) => c.value !== "-")
      .map((c) => ({
        value: c.value || "",
        label: `${c.value} (${c.count})`,
      })),
    types: (filtersData.types ?? []).map((l) => ({
      value: l.value || "",
      label: `${l.value} (${l.count})`,
    })),
    languageCodes: (filtersData.languageCodes ?? []).map((l) => ({
      value: l.value || "",
      label: `${l.value} (${l.count})`,
    })),
  };
}
